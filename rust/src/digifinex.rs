    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // createOrder
        //
        //     {
        //         "code": 0,
        //         "order_id": "198361cecdc65f9c8c9bb2fa68faec40"
        //     }
        //
        // fetchOrder, fetchOpenOrders, fetchOrders
        //
        //     {
        //         "symbol": "BTC_USDT",
        //         "order_id": "dd3164b333a4afa9d5730bb87f6db8b3",
        //         "created_date": 1562303547,
        //         "finished_date": 0,
        //         "price": 0.1,
        //         "amount": 1,
        //         "cash_amount": 1,
        //         "executed_amount": 0,
        //         "avg_price": 0,
        //         "status": 1,
        //         "type": "buy",
        //         "kind": "margin"
        //     }
        //
        let mut id: Value = self.safe_string(order.clone(), Value::from("order_id"), Value::Undefined);
        let mut timestamp: Value = self.safe_timestamp(order.clone(), Value::from("created_date"), Value::Undefined);
        let mut last_trade_timestamp: Value = self.safe_timestamp(order.clone(), Value::from("finished_date"), Value::Undefined);
        let mut side: Value = self.safe_string(order.clone(), Value::from("type"), Value::Undefined);
        let mut r#type: Value = Value::Undefined;
        if side.clone().is_nonnullish() {
            let mut parts: Value = side.split(Value::from("_"));
            let mut num_parts: Value = parts.len().into();
            if num_parts.clone() > Value::from(1) {
                side = parts.get(Value::from(0));
                r#type = parts.get(Value::from(1));
            } else {
                r#type = Value::from("limit");
            };
        };
        let mut status: Value = Digifinex::parse_order_status(self, self.safe_string(order.clone(), Value::from("status"), Value::Undefined));
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("symbol"), Value::Undefined);
        let mut symbol: Value = Digifinex::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut amount_string: Value = self.safe_string(order.clone(), Value::from("amount"), Value::Undefined);
        let mut filled_string: Value = self.safe_string(order.clone(), Value::from("executed_amount"), Value::Undefined);
        let mut price_string: Value = self.safe_string(order.clone(), Value::from("price"), Value::Undefined);
        let mut average_string: Value = self.safe_string(order.clone(), Value::from("avg_price"), Value::Undefined);
        return Digifinex::safe_order(self, Value::Json(normalize(&Value::Json(json!({
            "info": order,
            "id": id,
            "clientOrderId": Value::Undefined,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "lastTradeTimestamp": last_trade_timestamp,
            "symbol": symbol,
            "type": r#type,
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price_string,
            "stopPrice": Value::Undefined,
            "amount": amount_string,
            "filled": filled_string,
            "remaining": Value::Undefined,
            "cost": Value::Undefined,
            "average": average_string,
            "status": status,
            "fee": Value::Undefined,
            "trades": Value::Undefined
        }))).unwrap()), market.clone());
    }

