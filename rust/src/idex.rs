    fn parse_balance(&self, mut response: Value) -> Value {
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "info": response,
            "timestamp": Value::Undefined,
            "datetime": Value::Undefined
        }))).unwrap());
        let mut i: usize = 0;
        while i < response.len() {
            let mut entry: Value = response.get(i.into());
            let mut currency_id: Value = self.safe_string(entry.clone(), Value::from("asset"), Value::Undefined);
            let mut code: Value = Idex::safe_currency_code(self, currency_id.clone(), Value::Undefined);
            let mut account: Value = Idex::account(self);
            account.set("total".into(), self.safe_string(entry.clone(), Value::from("quantity"), Value::Undefined));
            account.set("free".into(), self.safe_string(entry.clone(), Value::from("availableForTrade"), Value::Undefined));
            account.set("used".into(), self.safe_string(entry.clone(), Value::from("locked"), Value::Undefined));
            result.set(code.clone(), account.clone());
            i += 1;
        };
        return Idex::safe_balance(self, result.clone());
    }

