    fn parse_side(&self, mut book: Value, mut side: Value) -> Value {
        let mut book_side: Value = self.safe_value(book.clone(), side.clone(), Value::new_array());
        let mut result: Value = Value::new_array();
        let mut i: usize = 0;
        while i < book_side.len() {
            let mut order: Value = book_side.get(i.into());
            let mut price: Value = Idex::safe_number(self, order.clone(), Value::from(0), Value::Undefined);
            let mut amount: Value = Idex::safe_number(self, order.clone(), Value::from(1), Value::Undefined);
            let mut order_count: Value = self.safe_integer(order.clone(), Value::from(2), Value::Undefined);
            result.push(Value::Json(serde_json::Value::Array(vec![price.clone().into(), amount.clone().into(), order_count.clone().into()])));
            i += 1;
        };
        let mut descending: Value = (side.clone() == Value::from("bids")).into();
        return self.sort_by(result.clone(), Value::from(0), descending.clone(), Value::Undefined);
    }

