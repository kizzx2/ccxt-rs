    /// Returns a [transaction structure](https://docs.ccxt.com/en/latest/manual.html#transaction-structure)
    ///
    /// Make a withdrawal
    ///
    /// # Arguments
    ///
    /// * `code` {string} - unified currency code
    /// * `amount` {float} - the amount to withdraw
    /// * `address` {string} - the address to withdraw to
    /// * `tag` {string|undefined} - 
    /// * `params` {object} - extra parameters specific to the bitpanda api endpoint
    async fn withdraw(&mut self, mut code: Value, mut amount: Value, mut address: Value, mut tag: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        (tag, params) = shift_2(Bitpanda::handle_withdraw_tag_and_params(self, tag.clone(), params.clone()));
        self.check_address(address.clone());
        Bitpanda::load_markets(self, Value::Undefined, Value::Undefined).await;
        let mut currency: Value = Bitpanda::currency(self, code.clone());
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "currency": code,
            "amount": Bitpanda::currency_to_precision(self, code.clone(), amount.clone(), Value::Undefined)
        }))).unwrap());
        // 'payout_account_id': '66756a10-3e86-48f4-9678-b634c4b135b2', // fiat only
        // 'recipient': { // crypto only
        //     'address': address,
        //     // 'destination_tag': '',
        // },
        let mut options: Value = self.safe_value(self.get("options".into()), Value::from("fiat"), Value::new_array());
        let mut is_fiat: Value = self.in_array(code.clone(), options.clone());
        let mut method: Value = if is_fiat.is_truthy() { Value::from("privatePostAccountWithdrawFiat") } else { Value::from("privatePostAccountWithdrawCrypto") };
        if is_fiat.is_truthy() {
            let mut payout_account_id: Value = self.safe_string(params.clone(), Value::from("payout_account_id"), Value::Undefined);
            if payout_account_id.clone().is_nullish() {
                panic!(r###"ArgumentsRequired::new(self.get("id".into()) + Value::from(" withdraw() requires a payout_account_id param for fiat ") + code.clone() + Value::from(" withdrawals"))"###);
            };
        } else {
            let mut recipient: Value = Value::Json(normalize(&Value::Json(json!({
                "address": address
            }))).unwrap());
            if tag.clone().is_nonnullish() {
                recipient.set("destination_tag".into(), tag.clone());
            };
            request.set("recipient".into(), recipient.clone());
        };
        let mut response: Value = Bitpanda::dispatch(self, method, extend_2(request.clone(), params.clone()), Value::Undefined).await;
        //
        // crypto
        //
        //     {
        //         "amount": "1234.5678",
        //         "fee": "1234.5678",
        //         "recipient": "3NacQ7rzZdhfyAtfJ5a11k8jFPdcMP2Bq7",
        //         "destination_tag": "",
        //         "transaction_id": "d0f8529f-f832-4e6a-9dc5-b8d5797badb2"
        //     }
        //
        // fiat
        //
        //     {
        //         "transaction_id": "54236cd0-4413-11e9-93fb-5fea7e5b5df6"
        //     }
        //
        return Bitpanda::parse_transaction(self, response.clone(), currency.clone());
    }

    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        //
        // fetchDeposits, fetchWithdrawals
        //
        //     {
        //         "transaction_id": "C2b42efcd-d5b7-4a56-8e12-b69ffd68c5ef",
        //         "type": "FIAT",
        //         "account_id": "c2d0076a-c20d-41f8-9e9a-1a1d028b2b58",
        //         "amount": "1234.5678",
        //         "time": "2019-08-24T14:15:22Z",
        //         "funds_source": "INTERNAL",
        //         "currency": "BTC",
        //         "fee_amount": "1234.5678",
        //         "fee_currency": "BTC",
        //         "blockchain_transaction_id": "f4184fc596403b9d638783cf57adfe4c75c605f6356fbc91338530e9831e9e16",
        //         "related_transaction_id": "e298341a-3855-405e-bce3-92db368a3157"
        //     }
        //
        // withdraw
        //
        //
        //     crypto
        //
        //     {
        //         "amount": "1234.5678",
        //         "fee": "1234.5678",
        //         "recipient": "3NacQ7rzZdhfyAtfJ5a11k8jFPdcMP2Bq7",
        //         "destination_tag": "",
        //         "transaction_id": "d0f8529f-f832-4e6a-9dc5-b8d5797badb2"
        //     }
        //
        //     fiat
        //
        //     {
        //         "transaction_id": "54236cd0-4413-11e9-93fb-5fea7e5b5df6"
        //     }
        //
        let mut id: Value = self.safe_string(transaction.clone(), Value::from("transaction_id"), Value::Undefined);
        let mut amount: Value = Bitpanda::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined);
        let mut timestamp: Value = self.parse8601(self.safe_string(transaction.clone(), Value::from("time"), Value::Undefined));
        let mut currency_id: Value = self.safe_string(transaction.clone(), Value::from("currency"), Value::Undefined);
        currency = Bitpanda::safe_currency(self, currency_id.clone(), currency.clone());
        let mut status: Value = Value::from("ok");
        // the exchange returns cleared transactions only
        let mut fee_cost: Value = Bitpanda::safe_number_2(self, transaction.clone(), Value::from("fee_amount"), Value::from("fee"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        let mut address_to: Value = self.safe_string(transaction.clone(), Value::from("recipient"), Value::Undefined);
        let mut tag_to: Value = self.safe_string(transaction.clone(), Value::from("destination_tag"), Value::Undefined);
        if fee_cost.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string(transaction.clone(), Value::from("fee_currency"), currency_id.clone());
            let mut fee_currency_code: Value = Bitpanda::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost,
                "currency": fee_currency_code
            }))).unwrap());
        };
        return Value::Json(normalize(&Value::Json(json!({
            "info": transaction,
            "id": id,
            "currency": currency.get(Value::from("code")),
            "amount": amount,
            "network": Value::Undefined,
            "address": address_to,
            "addressFrom": Value::Undefined,
            "addressTo": address_to,
            "tag": tag_to,
            "tagFrom": Value::Undefined,
            "tagTo": tag_to,
            "status": status,
            "type": Value::Undefined,
            "updated": Value::Undefined,
            "txid": self.safe_string(transaction.clone(), Value::from("blockchain_transaction_id"), Value::Undefined),
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "fee": fee
        }))).unwrap());
    }

