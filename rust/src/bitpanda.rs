    fn parse_ohlcv(&self, mut ohlcv: Value, mut market: Value) -> Value {
        //
        //     {
        //         "instrument_code":"BTC_EUR",
        //         "granularity":{"unit":"HOURS","period":1},
        //         "high":"9252.65",
        //         "low":"9115.27",
        //         "open":"9250.0",
        //         "close":"9132.35",
        //         "total_amount":"33.85924",
        //         "volume":"311958.9635744",
        //         "time":"2020-05-08T22:59:59.999Z",
        //         "last_sequence":461123
        //     }
        //
        let mut granularity: Value = self.safe_value(ohlcv.clone(), Value::from("granularity"), Value::Undefined);
        let mut unit: Value = self.safe_string(granularity.clone(), Value::from("unit"), Value::Undefined);
        let mut period: Value = self.safe_string(granularity.clone(), Value::from("period"), Value::Undefined);
        let mut units: Value = Value::Json(normalize(&Value::Json(json!({
            "MINUTES": "m",
            "HOURS": "h",
            "DAYS": "d",
            "WEEKS": "w",
            "MONTHS": "M"
        }))).unwrap());
        let mut lowercase_unit: Value = self.safe_string(units.clone(), unit.clone(), Value::Undefined);
        let mut timeframe: Value = period.clone() + lowercase_unit.clone();
        let mut duration_in_seconds: Value = self.parse_timeframe(timeframe.clone());
        let mut duration: Value = duration_in_seconds.clone() * Value::from(1000);
        let mut timestamp: Value = self.parse8601(self.safe_string(ohlcv.clone(), Value::from("time"), Value::Undefined));
        let mut aligned_timestamp: Value = duration.clone() * parse_int(timestamp.clone() / duration.clone());
        let mut options: Value = self.safe_value(self.get("options".into()), Value::from("fetchOHLCV"), Value::new_object());
        let mut volume_field: Value = self.safe_string(options.clone(), Value::from("volume"), Value::from("total_amount"));
        return Value::Json(serde_json::Value::Array(vec![aligned_timestamp.clone().into(), Bitpanda::safe_number(self, ohlcv.clone(), Value::from("open"), Value::Undefined).into(), Bitpanda::safe_number(self, ohlcv.clone(), Value::from("high"), Value::Undefined).into(), Bitpanda::safe_number(self, ohlcv.clone(), Value::from("low"), Value::Undefined).into(), Bitpanda::safe_number(self, ohlcv.clone(), Value::from("close"), Value::Undefined).into(), Bitpanda::safe_number(self, ohlcv.clone(), volume_field.clone(), Value::Undefined).into()]));
    }

    /// Returns a list of candles ordered as timestamp, open, high, low, close, volume
    ///
    /// Fetches historical candlestick data containing the open, high, low, and close price, and the volume of a market
    ///
    /// # Arguments
    ///
    /// * `symbol` {string} - unified symbol of the market to fetch OHLCV data for
    /// * `timeframe` {string} - the length of time each candle represents
    /// * `since` {int|undefined} - timestamp in ms of the earliest candle to fetch
    /// * `limit` {int|undefined} - the maximum amount of candles to fetch
    /// * `params` {object} - extra parameters specific to the bitpanda api endpoint
    async fn fetch_ohlcv(&mut self, mut symbol: Value, mut timeframe: Value, mut since: Value, mut limit: Value, mut params: Value) -> Value {
        timeframe = timeframe.or_default(Value::from("1m"));
        params = params.or_default(Value::new_object());
        Bitpanda::load_markets(self, Value::Undefined, Value::Undefined).await;
        let mut market: Value = Bitpanda::market(self, symbol.clone());
        let mut period_unit: Value = self.safe_string(self.get("timeframes".into()), timeframe.clone(), Value::Undefined);
        let (mut period, mut unit) = shift_2(period_unit.split(Value::from("/")));
        let mut duration_in_seconds: Value = self.parse_timeframe(timeframe.clone());
        let mut duration: Value = duration_in_seconds.clone() * Value::from(1000);
        if limit.clone().is_nullish() {
            limit = Value::from(1500);
        };
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "instrument_code": market.get(Value::from("id")),
            "period": period,
            "unit": unit
        }))).unwrap());
        // 'from': this.iso8601 (since),
        // 'to': this.iso8601 (this.milliseconds ()),
        if since.clone().is_nullish() {
            let mut now: Value = self.milliseconds();
            request.set("to".into(), self.iso8601(now.clone()));
            request.set("from".into(), self.iso8601(now.clone() - limit.clone() * duration.clone()));
        } else {
            request.set("from".into(), self.iso8601(since.clone()));
            request.set("to".into(), self.iso8601(self.sum(since.clone(), limit.clone() * duration.clone())));
        };
        let mut response: Value = Bitpanda::dispatch(self, "publicGetCandlesticksInstrumentCode".into(), extend_2(request.clone(), params.clone()), Value::Undefined).await;
        //
        //     [
        //         {"instrument_code":"BTC_EUR","granularity":{"unit":"HOURS","period":1},"high":"9252.65","low":"9115.27","open":"9250.0","close":"9132.35","total_amount":"33.85924","volume":"311958.9635744","time":"2020-05-08T22:59:59.999Z","last_sequence":461123},
        //         {"instrument_code":"BTC_EUR","granularity":{"unit":"HOURS","period":1},"high":"9162.49","low":"9040.0","open":"9132.53","close":"9083.69","total_amount":"26.19685","volume":"238553.7812365","time":"2020-05-08T23:59:59.999Z","last_sequence":461376},
        //         {"instrument_code":"BTC_EUR","granularity":{"unit":"HOURS","period":1},"high":"9135.7","low":"9002.59","open":"9055.45","close":"9133.98","total_amount":"26.21919","volume":"238278.8724959","time":"2020-05-09T00:59:59.999Z","last_sequence":461521},
        //     ]
        //
        return Bitpanda::parse_ohlcvs(self, response.clone(), market.clone(), timeframe.clone(), since.clone(), limit.clone());
    }

    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // fetchTrades (public)
        //
        //     {
        //         "instrument_code":"BTC_EUR",
        //         "price":"8137.28",
        //         "amount":"0.22269",
        //         "taker_side":"BUY",
        //         "volume":"1812.0908832",
        //         "time":"2020-07-10T14:44:32.299Z",
        //         "trade_timestamp":1594392272299,
        //         "sequence":603047
        //     }
        //
        // fetchMyTrades, fetchOrder, fetchOpenOrders, fetchClosedOrders trades (private)
        //
        //     {
        //         "fee": {
        //             "fee_amount": "0.0014",
        //             "fee_currency": "BTC",
        //             "fee_percentage": "0.1",
        //             "fee_group_id": "default",
        //             "fee_type": "TAKER",
        //             "running_trading_volume": "0.0"
        //         },
        //         "trade": {
        //             "trade_id": "fdff2bcc-37d6-4a2d-92a5-46e09c868664",
        //             "order_id": "36bb2437-7402-4794-bf26-4bdf03526439",
        //             "account_id": "a4c699f6-338d-4a26-941f-8f9853bfc4b9",
        //             "amount": "1.4",
        //             "side": "BUY",
        //             "instrument_code": "BTC_EUR",
        //             "price": "7341.4",
        //             "time": "2019-09-27T15:05:32.564Z",
        //             "sequence": 48670
        //         }
        //     }
        //
        let mut fee_info: Value = self.safe_value(trade.clone(), Value::from("fee"), Value::new_object());
        trade = self.safe_value(trade.clone(), Value::from("trade"), trade.clone());
        let mut timestamp: Value = self.safe_integer(trade.clone(), Value::from("trade_timestamp"), Value::Undefined);
        if timestamp.clone().is_nullish() {
            timestamp = self.parse8601(self.safe_string(trade.clone(), Value::from("time"), Value::Undefined));
        };
        let mut side: Value = self.safe_string_lower_2(trade.clone(), Value::from("side"), Value::from("taker_side"), Value::Undefined);
        let mut price_string: Value = self.safe_string(trade.clone(), Value::from("price"), Value::Undefined);
        let mut amount_string: Value = self.safe_string(trade.clone(), Value::from("amount"), Value::Undefined);
        let mut cost_string: Value = self.safe_string(trade.clone(), Value::from("volume"), Value::Undefined);
        let mut market_id: Value = self.safe_string(trade.clone(), Value::from("instrument_code"), Value::Undefined);
        let mut symbol: Value = Bitpanda::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut fee_cost_string: Value = self.safe_string(fee_info.clone(), Value::from("fee_amount"), Value::Undefined);
        let mut taker_or_maker: Value = Value::Undefined;
        let mut fee: Value = Value::Undefined;
        if fee_cost_string.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string(fee_info.clone(), Value::from("fee_currency"), Value::Undefined);
            let mut fee_currency_code: Value = Bitpanda::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            let mut fee_rate_string: Value = self.safe_string(fee_info.clone(), Value::from("fee_percentage"), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost_string,
                "currency": fee_currency_code,
                "rate": fee_rate_string
            }))).unwrap());
            taker_or_maker = self.safe_string_lower(fee_info.clone(), Value::from("fee_type"), Value::Undefined);
        };
        return Bitpanda::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "id": self.safe_string_2(trade.clone(), Value::from("trade_id"), Value::from("sequence"), Value::Undefined),
            "order": self.safe_string(trade.clone(), Value::from("order_id"), Value::Undefined),
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "type": Value::Undefined,
            "side": side,
            "price": price_string,
            "amount": amount_string,
            "cost": cost_string,
            "takerOrMaker": taker_or_maker,
            "fee": fee,
            "info": trade
        }))).unwrap()), market.clone());
    }

