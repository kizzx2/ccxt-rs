    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // createOrder, cancelOrder, fetchOpenOrders, fetchClosedOrders, fetchOrders, fetchOrder
        //
        //     {
        //         "order_id": "286e560e-b8a2-464b-8b84-15a7e2a67eab",
        //         "pair": "BTC_ARS",
        //         "side": "SELL",
        //         "amount": "0.00100",
        //         "notional": null,
        //         "fill_or_kill": false,
        //         "all_or_none": false,
        //         "order_type": "LIMIT",
        //         "status": "CANC",
        //         "created_at": 1575472707,
        //         "filled": "0.00000",
        //         "limit_price": "681000.00",
        //         "stop_price": null,
        //         "distance": null
        //     }
        //
        //     {
        //         "order_id":"d6b60c01-8624-44f2-9e6c-9e8cd677ea5c",
        //         "pair":"BTC_USDC",
        //         "side":"BUY",
        //         "amount":"0.00200",
        //         "notional":"50",
        //         "fill_or_kill":false,
        //         "all_or_none":false,
        //         "order_type":"MARKET",
        //         "status":"OPEN",
        //         "created_at":1601730306,
        //         "filled":"0.00000",
        //         "fill_price":10593.99,
        //         "fee":0.0,
        //         "fills":[
        //             {
        //                 "pair":"BTC_USDC",
        //                 "exchanged":0.002,
        //                 "match_price":10593.99,
        //                 "maker_fee":0.0,
        //                 "taker_fee":0.0,
        //                 "timestamp":1601730306942
        //             }
        //         ],
        //         "filled_at":"2020-10-03T13:05:06.942186Z",
        //         "limit_price":"0.000000",
        //         "stop_price":null,
        //         "distance":null
        //     }
        //
        let mut id: Value = self.safe_string(order.clone(), Value::from("order_id"), Value::Undefined);
        let mut amount: Value = Ripio::safe_number(self, order.clone(), Value::from("amount"), Value::Undefined);
        let mut cost: Value = Ripio::safe_number(self, order.clone(), Value::from("notional"), Value::Undefined);
        let mut r#type: Value = self.safe_string_lower(order.clone(), Value::from("order_type"), Value::Undefined);
        let mut price_field: Value = if r#type.clone() == Value::from("market") { Value::from("fill_price") } else { Value::from("limit_price") };
        let mut price: Value = Ripio::safe_number(self, order.clone(), price_field.clone(), Value::Undefined);
        let mut side: Value = self.safe_string_lower(order.clone(), Value::from("side"), Value::Undefined);
        let mut status: Value = Ripio::parse_order_status(self, self.safe_string(order.clone(), Value::from("status"), Value::Undefined));
        let mut timestamp: Value = self.safe_timestamp(order.clone(), Value::from("created_at"), Value::Undefined);
        let mut average: Value = self.safe_value(order.clone(), Value::from("fill_price"), Value::Undefined);
        let mut filled: Value = Ripio::safe_number(self, order.clone(), Value::from("filled"), Value::Undefined);
        let mut remaining: Value = Value::Undefined;
        let mut fills: Value = self.safe_value(order.clone(), Value::from("fills"), Value::Undefined);
        let mut trades: Value = Value::Undefined;
        let mut last_trade_timestamp: Value = Value::Undefined;
        if fills.clone().is_nonnullish() {
            let mut num_fills: Value = fills.len().into();
            if num_fills.clone() > Value::from(0) {
                filled = Value::from(0);
                cost = Value::from(0);
                trades = Ripio::parse_trades(self, fills.clone(), market.clone(), Value::Undefined, Value::Undefined, Value::Json(normalize(&Value::Json(json!({
                    "order": id,
                    "side": side
                }))).unwrap()));
                let mut i: usize = 0;
                while i < trades.len() {
                    let mut trade: Value = trades.get(i.into());
                    filled = self.sum(trade.get(Value::from("amount")), filled.clone());
                    cost = self.sum(trade.get(Value::from("cost")), cost.clone());
                    last_trade_timestamp = trade.get(Value::from("timestamp"));
                    i += 1;
                };
                if average.clone().is_nullish() && filled.clone() > Value::from(0) {
                    average = cost.clone() / filled.clone();
                };
            };
        };
        if filled.clone().is_nonnullish() {
            if cost.clone().is_nullish() && price.clone().is_nonnullish() {
                cost = price.clone() * filled.clone();
            };
            if amount.clone().is_nonnullish() {
                remaining = Math::max(Value::from(0), amount.clone() - filled.clone());
            };
        };
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("pair"), Value::Undefined);
        let mut symbol: Value = Ripio::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut stop_price: Value = Ripio::safe_number(self, order.clone(), Value::from("stop_price"), Value::Undefined);
        return Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": Value::Undefined,
            "info": order,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "lastTradeTimestamp": last_trade_timestamp,
            "symbol": symbol,
            "type": r#type,
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price,
            "stopPrice": stop_price,
            "amount": amount,
            "cost": cost,
            "average": average,
            "filled": filled,
            "remaining": remaining,
            "status": status,
            "fee": Value::Undefined,
            "trades": trades
        }))).unwrap());
    }

    /// Returns a list of [trade structures](https://docs.ccxt.com/en/latest/manual.html#trade-structure)
