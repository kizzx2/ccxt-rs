    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // fetchOrders, fetchOpenOrders, fetchClosedOrders, fetchCanceledOrders
        //
        //     {
        //         "order_id": "14",
        //         "created": "1435517311",
        //         "type": "buy",
        //         "pair": "BTC_USD",
        //         "price": "100",
        //         "quantity": "1",
        //         "amount": "100"
        //     }
        //
        // fetchOrder
        //
        //     {
        //         "type": "buy",
        //         "in_currency": "BTC",
        //         "in_amount": "1",
        //         "out_currency": "USD",
        //         "out_amount": "100",
        //         "trades": [
        //             {
        //                 "trade_id": 3,
        //                 "date": 1435488248,
        //                 "type": "buy",
        //                 "pair": "BTC_USD",
        //                 "order_id": 12345,
        //                 "quantity": 1,
        //                 "price": 100,
        //                 "amount": 100
        //             }
        //         ]
        //     }
        //
        let mut id: Value = self.safe_string(order.clone(), Value::from("order_id"), Value::Undefined);
        let mut timestamp: Value = self.safe_timestamp(order.clone(), Value::from("created"), Value::Undefined);
        let mut symbol: Value = Value::Undefined;
        let mut side: Value = self.safe_string(order.clone(), Value::from("type"), Value::Undefined);
        let mut market_id: Value = Value::Undefined;
        if order.contains_key(Value::from("pair")) {
            market_id = order.get(Value::from("pair"));
        } else if order.contains_key(Value::from("in_currency")) && order.contains_key(Value::from("out_currency")) {
            if side.clone() == Value::from("buy") {
                market_id = order.get(Value::from("in_currency")) + Value::from("_") + order.get(Value::from("out_currency"));
            } else {
                market_id = order.get(Value::from("out_currency")) + Value::from("_") + order.get(Value::from("in_currency"));
            };
        };
        if market_id.clone().is_nonnullish() && self.get("markets_by_id".into()).contains_key(market_id.clone()) {
            market = self.get("markets_by_id".into()).get(market_id.clone());
        };
        let mut amount: Value = Exmo::safe_number(self, order.clone(), Value::from("quantity"), Value::Undefined);
        if amount.clone().is_nullish() {
            let mut amount_field: Value = if side.clone() == Value::from("buy") { Value::from("in_amount") } else { Value::from("out_amount") };
            amount = Exmo::safe_number(self, order.clone(), amount_field.clone(), Value::Undefined);
        };
        let mut price: Value = Exmo::safe_number(self, order.clone(), Value::from("price"), Value::Undefined);
        let mut cost: Value = Exmo::safe_number(self, order.clone(), Value::from("amount"), Value::Undefined);
        let mut filled: Value = Value::from(0);
        let mut trades: Value = Value::new_array();
        let mut transactions: Value = self.safe_value(order.clone(), Value::from("trades"), Value::new_array());
        let mut fee_cost: Value = Value::Undefined;
        let mut last_trade_timestamp: Value = Value::Undefined;
        let mut average: Value = Value::Undefined;
        let mut num_transactions: Value = transactions.len().into();
        if num_transactions.clone() > Value::from(0) {
            fee_cost = Value::from(0);
            let mut i: usize = 0;
            while i < num_transactions.clone().into() {
                let mut trade: Value = Exmo::parse_trade(self, transactions.get(i.into()), market.clone());
                if id.clone().is_nullish() {
                    id = trade.get(Value::from("order"));
                };
                if timestamp.clone().is_nullish() {
                    timestamp = trade.get(Value::from("timestamp"));
                };
                if timestamp.clone() > trade.get(Value::from("timestamp")) {
                    timestamp = trade.get(Value::from("timestamp"));
                };
                filled = self.sum(filled.clone(), trade.get(Value::from("amount")));
                fee_cost = self.sum(fee_cost.clone(), trade.get(Value::from("fee")).get(Value::from("cost")));
                trades.push(trade.clone());
                i += 1;
            };
            last_trade_timestamp = trades.get(num_transactions.clone() - Value::from(1).clone()).get(Value::from("timestamp"));
        };
        let mut status: Value = self.safe_string(order.clone(), Value::from("status"), Value::Undefined);
        // in case we need to redefine it for canceled orders
        let mut remaining: Value = Value::Undefined;
        if amount.clone().is_nonnullish() {
            remaining = amount.clone() - filled.clone();
            if filled.clone() >= amount.clone() {
                status = Value::from("closed");
            } else {
                status = Value::from("open");
            };
        };
        if market.clone().is_nullish() {
            market = Exmo::get_market_from_trades(self, trades.clone());
        };
        let mut fee_currency: Value = Value::Undefined;
        if market.clone().is_nonnullish() {
            symbol = market.get(Value::from("symbol"));
            fee_currency = market.get(Value::from("quote"));
        };
        if cost.clone().is_nullish() {
            if price.clone().is_nonnullish() {
                cost = price.clone() * filled.clone();
            };
        } else {
            if filled.clone() > Value::from(0) {
                if average.clone().is_nullish() {
                    average = cost.clone() / filled.clone();
                };
                if price.clone().is_nullish() {
                    price = cost.clone() / filled.clone();
                };
            };
        };
        let mut fee: Value = Value::Json(normalize(&Value::Json(json!({
            "cost": fee_cost,
            "currency": fee_currency
        }))).unwrap());
        let mut client_order_id: Value = self.safe_integer(order.clone(), Value::from("client_id"), Value::Undefined);
        return Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": client_order_id,
            "datetime": self.iso8601(timestamp.clone()),
            "timestamp": timestamp,
            "lastTradeTimestamp": last_trade_timestamp,
            "status": status,
            "symbol": symbol,
            "type": "limit",
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price,
            "stopPrice": Value::Undefined,
            "cost": cost,
            "amount": amount,
            "filled": filled,
            "remaining": remaining,
            "average": average,
            "trades": trades,
            "fee": fee,
            "info": order
        }))).unwrap());
    }

