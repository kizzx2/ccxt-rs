    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // fetchTrades (public)
        //
        //     {
        //         "trade_id":165087520,
        //         "date":1587470005,
        //         "type":"buy",
        //         "quantity":"1.004",
        //         "price":"0.02491461",
        //         "amount":"0.02501426"
        //     },
        //
        // fetchMyTrades, fetchOrderTrades
        //
        //     {
        //         "trade_id": 3,
        //         "date": 1435488248,
        //         "type": "buy",
        //         "pair": "BTC_USD",
        //         "order_id": 12345,
        //         "quantity": 1,
        //         "price": 100,
        //         "amount": 100,
        //         "exec_type": "taker",
        //         "commission_amount": "0.02",
        //         "commission_currency": "BTC",
        //         "commission_percent": "0.2"
        //     }
        //
        let mut timestamp: Value = self.safe_timestamp(trade.clone(), Value::from("date"), Value::Undefined);
        let mut symbol: Value = Value::Undefined;
        let mut id: Value = self.safe_string(trade.clone(), Value::from("trade_id"), Value::Undefined);
        let mut order_id: Value = self.safe_string(trade.clone(), Value::from("order_id"), Value::Undefined);
        let mut price_string: Value = self.safe_string(trade.clone(), Value::from("price"), Value::Undefined);
        let mut amount_string: Value = self.safe_string(trade.clone(), Value::from("quantity"), Value::Undefined);
        let mut cost_string: Value = self.safe_string(trade.clone(), Value::from("amount"), Value::Undefined);
        let mut side: Value = self.safe_string(trade.clone(), Value::from("type"), Value::Undefined);
        let mut r#type: Value = Value::Undefined;
        let mut market_id: Value = self.safe_string(trade.clone(), Value::from("pair"), Value::Undefined);
        if market_id.clone().is_nonnullish() {
            if self.get("markets_by_id".into()).contains_key(market_id.clone()) {
                market = self.get("markets_by_id".into()).get(market_id.clone());
            } else {
                let (mut base_id, mut quote_id) = shift_2(market_id.split(Value::from("_")));
                let mut base: Value = Exmo::safe_currency_code(self, base_id.clone(), Value::Undefined);
                let mut quote: Value = Exmo::safe_currency_code(self, quote_id.clone(), Value::Undefined);
                symbol = base.clone() + Value::from("/") + quote.clone();
            };
        };
        if symbol.clone().is_nullish() && market.clone().is_nonnullish() {
            symbol = market.get(Value::from("symbol"));
        };
        let mut taker_or_maker: Value = self.safe_string(trade.clone(), Value::from("exec_type"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        let mut fee_cost_string: Value = self.safe_string(trade.clone(), Value::from("commission_amount"), Value::Undefined);
        if fee_cost_string.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string(trade.clone(), Value::from("commission_currency"), Value::Undefined);
            let mut fee_currency_code: Value = Exmo::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            let mut fee_rate_string: Value = self.safe_string(trade.clone(), Value::from("commission_percent"), Value::Undefined);
            if fee_rate_string.clone().is_nonnullish() {
                fee_rate_string = Precise::string_div(fee_rate_string.clone(), Value::from("1000"), Value::from(18));
            };
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost_string,
                "currency": fee_currency_code,
                "rate": fee_rate_string
            }))).unwrap());
        };
        return Exmo::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "info": trade,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "order": order_id,
            "type": r#type,
            "side": side,
            "takerOrMaker": taker_or_maker,
            "price": price_string,
            "amount": amount_string,
            "cost": cost_string,
            "fee": fee
        }))).unwrap()), market.clone());
    }

