    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        //
        // fetchTransactions
        //
        //          {
        //            "dt": 1461841192,
        //            "type": "deposit",
        //            "curr": "RUB",
        //            "status": "processing",
        //            "provider": "Qiwi (LA) [12345]",
        //            "amount": "1",
        //            "account": "",
        //            "txid": "ec46f784ad976fd7f7539089d1a129fe46...",
        //          }
        //
        // fetchWithdrawals
        //
        //          {
        //             "operation_id": 47412538520634344,
        //             "created": 1573760013,
        //             "updated": 1573760013,
        //             "type": "withdraw",
        //             "currency": "DOGE",
        //             "status": "Paid",
        //             "amount": "300",
        //             "provider": "DOGE",
        //             "commission": "0",
        //             "account": "DOGE: DBVy8pF1f8yxaCVEHqHeR7kkcHecLQ8nRS",
        //             "order_id": 69670170,
        //             "provider_type": "crypto",
        //             "crypto_address": "DBVy8pF1f8yxaCVEHqHeR7kkcHecLQ8nRS",
        //             "card_number": "",
        //             "wallet_address": "",
        //             "email": "",
        //             "phone": "",
        //             "extra": {
        //                 "txid": "f2b66259ae1580f371d38dd27e31a23fff8c04122b65ee3ab5a3f612d579c792",
        //                 "confirmations": null,
        //                 "excode": "",
        //                 "invoice": ""
        //             },
        //             "error": ""
        //          },
        //
        let mut id: Value = self.safe_string_2(transaction.clone(), Value::from("order_id"), Value::from("task_id"), Value::Undefined);
        let mut timestamp: Value = self.safe_timestamp_2(transaction.clone(), Value::from("dt"), Value::from("created"), Value::Undefined);
        let mut updated: Value = self.safe_timestamp(transaction.clone(), Value::from("updated"), Value::Undefined);
        let mut amount: Value = Exmo::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined);
        if amount.clone().is_nonnullish() {
            amount = Math::abs(amount.clone());
        };
        let mut status: Value = Exmo::parse_transaction_status(self, self.safe_string_lower(transaction.clone(), Value::from("status"), Value::Undefined));
        let mut txid: Value = self.safe_string(transaction.clone(), Value::from("txid"), Value::Undefined);
        if txid.clone().is_nullish() {
            let mut extra: Value = self.safe_value(transaction.clone(), Value::from("extra"), Value::new_object());
            let mut extra_txid: Value = self.safe_string(extra.clone(), Value::from("txid"), Value::Undefined);
            if extra_txid.clone() != Value::from("") {
                txid = extra_txid.clone();
            };
        };
        let mut r#type: Value = self.safe_string(transaction.clone(), Value::from("type"), Value::Undefined);
        let mut currency_id: Value = self.safe_string_2(transaction.clone(), Value::from("curr"), Value::from("currency"), Value::Undefined);
        let mut code: Value = Exmo::safe_currency_code(self, currency_id.clone(), currency.clone());
        let mut address: Value = Value::Undefined;
        let mut tag: Value = Value::Undefined;
        let mut comment: Value = Value::Undefined;
        let mut account: Value = self.safe_string(transaction.clone(), Value::from("account"), Value::Undefined);
        if r#type.clone() == Value::from("deposit") {
            comment = account.clone();
        } else if r#type.clone() == Value::from("withdrawal") {
            address = account.clone();
            if address.clone().is_nonnullish() {
                let mut parts: Value = address.split(Value::from(":"));
                let mut num_parts: Value = parts.len().into();
                if num_parts.clone() == Value::from(2) {
                    address = self.safe_string(parts.clone(), Value::from(1), Value::Undefined);
                    address = address.replace(Value::from(" "), Value::from(""));
                };
            };
        };
        let mut fee: Value = Value::Undefined;
        // fixed funding fees only (for now)
        if !self.get("fees".into()).get(Value::from("transaction")).get(Value::from("percentage")).is_truthy() {
            let mut key: Value = if r#type.clone() == Value::from("withdrawal") { Value::from("withdraw") } else { Value::from("deposit") };
            let mut fee_cost: Value = Exmo::safe_number(self, transaction.clone(), Value::from("commission"), Value::Undefined);
            if fee_cost.clone().is_nullish() {
                fee_cost = Exmo::safe_number(self, self.get("options".into()).get(Value::from("transactionFees")).get(key.clone()), code.clone(), Value::Undefined);
            };
            // users don't pay for cashbacks, no fees for that
            let mut provider: Value = self.safe_string(transaction.clone(), Value::from("provider"), Value::Undefined);
            if provider.clone() == Value::from("cashback") {
                fee_cost = Value::from(0);
            };
            if fee_cost.clone().is_nonnullish() {
                // withdrawal amount includes the fee
                if r#type.clone() == Value::from("withdrawal") {
                    amount = amount.clone() - fee_cost.clone();
                };
                fee = Value::Json(normalize(&Value::Json(json!({
                    "cost": fee_cost,
                    "currency": code,
                    "rate": Value::Undefined
                }))).unwrap());
            };
        };
        let mut network: Value = self.safe_string(transaction.clone(), Value::from("provider"), Value::Undefined);
        return Value::Json(normalize(&Value::Json(json!({
            "info": transaction,
            "id": id,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "currency": code,
            "amount": amount,
            "network": network,
            "address": address,
            "addressTo": address,
            "addressFrom": Value::Undefined,
            "tag": tag,
            "tagTo": tag,
            "tagFrom": Value::Undefined,
            "status": status,
            "type": r#type,
            "updated": updated,
            "comment": comment,
            "txid": txid,
            "fee": fee
        }))).unwrap());
    }

