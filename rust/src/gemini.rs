    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        //
        // withdraw
        //
        //   for BTC
        //     {
        //         "address":"mi98Z9brJ3TgaKsmvXatuRahbFRUFKRUdR",
        //         "amount":"1",
        //         "withdrawalId":"02176a83-a6b1-4202-9b85-1c1c92dd25c4",
        //         "message":"You have requested a transfer of 1 BTC to mi98Z9brJ3TgaKsmvXatuRahbFRUFKRUdR. This withdrawal will be sent to the blockchain within the next 60 seconds."
        //     }
        //
        //   for ETH
        //     {
        //         "address":"0xA63123350Acc8F5ee1b1fBd1A6717135e82dBd28",
        //         "amount":"2.34567",
        //         "txHash":"0x28267179f92926d85c5516bqc063b2631935573d8915258e95d9572eedcc8cc"
        //     }
        //
        let mut timestamp: Value = self.safe_integer(transaction.clone(), Value::from("timestampms"), Value::Undefined);
        let mut currency_id: Value = self.safe_string(transaction.clone(), Value::from("currency"), Value::Undefined);
        let mut code: Value = Gemini::safe_currency_code(self, currency_id.clone(), currency.clone());
        let mut address: Value = self.safe_string(transaction.clone(), Value::from("destination"), Value::Undefined);
        let mut r#type: Value = self.safe_string_lower(transaction.clone(), Value::from("type"), Value::Undefined);
        // if status field is available, then it's complete
        let mut status_raw: Value = self.safe_string(transaction.clone(), Value::from("status"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        let mut fee_amount: Value = Gemini::safe_number(self, transaction.clone(), Value::from("feeAmount"), Value::Undefined);
        if fee_amount.clone().is_nonnullish() {
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_amount,
                "currency": code
            }))).unwrap());
        };
        return Value::Json(normalize(&Value::Json(json!({
            "info": transaction,
            "id": self.safe_string_2(transaction.clone(), Value::from("eid"), Value::from("withdrawalId"), Value::Undefined),
            "txid": self.safe_string(transaction.clone(), Value::from("txHash"), Value::Undefined),
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "network": Value::Undefined,
            "address": address,
            "addressTo": Value::Undefined,
            "addressFrom": Value::Undefined,
            "tag": Value::Undefined,
            "tagTo": Value::Undefined,
            "tagFrom": Value::Undefined,
            "type": r#type,
            "amount": Gemini::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined),
            "currency": code,
            "status": Gemini::parse_transaction_status(self, status_raw.clone()),
            "updated": Value::Undefined,
            "fee": fee
        }))).unwrap());
    }

