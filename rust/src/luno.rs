    /// Returns a [ledger structure](https://docs.ccxt.com/en/latest/manual.html#ledger-structure)
    ///
    /// Fetch the history of changes, actions done by the user or operations that altered balance of the user
    ///
    /// # Arguments
    ///
    /// * `code` {string|undefined} - unified currency code, default is undefined
    /// * `since` {int|undefined} - timestamp in ms of the earliest ledger entry, default is undefined
    /// * `limit` {int|undefined} - max number of ledger entrys to return, default is undefined
    /// * `params` {object} - extra parameters specific to the luno api endpoint
    async fn fetch_ledger(&mut self, mut code: Value, mut since: Value, mut limit: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        Luno::load_markets(self, Value::Undefined, Value::Undefined).await;
        Luno::load_accounts(self, Value::Undefined, Value::Undefined).await;
        let mut currency: Value = Value::Undefined;
        let mut id: Value = self.safe_string(params.clone(), Value::from("id"), Value::Undefined);
        // account id
        let mut min_row: Value = self.safe_value(params.clone(), Value::from("min_row"), Value::Undefined);
        let mut max_row: Value = self.safe_value(params.clone(), Value::from("max_row"), Value::Undefined);
        if id.clone().is_nullish() {
            if code.clone().is_nullish() {
                panic!(r###"ArgumentsRequired::new(self.get("id".into()) + Value::from(" fetchLedger() requires a currency code argument if no account id specified in params"))"###);
            };
            currency = Luno::currency(self, code.clone());
            let mut accounts_by_currency_code: Value = self.index_by(self.get("accounts".into()), Value::from("currency"), Value::Undefined);
            let mut account: Value = self.safe_value(accounts_by_currency_code.clone(), code.clone(), Value::Undefined);
            if account.clone().is_nullish() {
                panic!(r###"ExchangeError::new(self.get("id".into()) + Value::from(" fetchLedger() could not find account id for ") + code.clone())"###);
            };
            id = account.get(Value::from("id"));
        };
        if min_row.clone().is_nullish() && max_row.clone().is_nullish() {
            max_row = Value::from(0);
            // Default to most recent transactions
            min_row = Value::from(1000).neg();
        } else if min_row.clone().is_nullish() || max_row.clone().is_nullish() {
            // Maximum number of records supported
            panic!(r###"ExchangeError::new(self.get("id".into()) + Value::from(" fetchLedger() require both params 'max_row' and 'min_row' or neither to be defined"))"###);
        };
        if limit.clone().is_nonnullish() && max_row.clone() - min_row.clone() > limit.clone() {
            if max_row.clone() <= Value::from(0) {
                min_row = max_row.clone() - limit.clone();
            } else if min_row.clone() > Value::from(0) {
                max_row = min_row.clone() + limit.clone();
            };
        };
        if max_row.clone() - min_row.clone() > Value::from(1000) {
            panic!(r###"ExchangeError::new(self.get("id".into()) + Value::from(" fetchLedger() requires the params 'max_row' - 'min_row' <= 1000"))"###);
        };
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "min_row": min_row,
            "max_row": max_row
        }))).unwrap());
        let mut response: Value = Luno::dispatch(self, "privateGetAccountsIdTransactions".into(), extend_2(params.clone(), request.clone()), Value::Undefined).await;
        let mut entries: Value = self.safe_value(response.clone(), Value::from("transactions"), Value::new_array());
        return Luno::parse_ledger(self, entries.clone(), currency.clone(), since.clone(), limit.clone(), Value::Undefined);
    }

    fn parse_ledger_comment(&self, mut comment: Value) -> Value {
        let mut words: Value = comment.split(Value::from(" "));
        let mut types: Value = Value::Json(normalize(&Value::Json(json!({
            "Withdrawal": "fee",
            "Trading": "fee",
            "Payment": "transaction",
            "Sent": "transaction",
            "Deposit": "transaction",
            "Received": "transaction",
            "Released": "released",
            "Reserved": "reserved",
            "Sold": "trade",
            "Bought": "trade",
            "Failure": "failed"
        }))).unwrap());
        let mut reference_id: Value = Value::Undefined;
        let mut first_word: Value = self.safe_string(words.clone(), Value::from(0), Value::Undefined);
        let mut third_word: Value = self.safe_string(words.clone(), Value::from(2), Value::Undefined);
        let mut fourth_word: Value = self.safe_string(words.clone(), Value::from(3), Value::Undefined);
        let mut r#type: Value = self.safe_string(types.clone(), first_word.clone(), Value::Undefined);
        if r#type.clone().is_nullish() && third_word.clone() == Value::from("fee") {
            r#type = Value::from("fee");
        };
        if r#type.clone() == Value::from("reserved") && fourth_word.clone() == Value::from("order") {
            reference_id = self.safe_string(words.clone(), Value::from(4), Value::Undefined);
        };
        return Value::Json(normalize(&Value::Json(json!({
            "type": r#type,
            "referenceId": reference_id
        }))).unwrap());
    }

    fn parse_ledger_entry(&self, mut entry: Value, mut currency: Value) -> Value {
        // const details = this.safeValue (entry, 'details', {});
        let mut id: Value = self.safe_string(entry.clone(), Value::from("row_index"), Value::Undefined);
        let mut account_id: Value = self.safe_string(entry.clone(), Value::from("account_id"), Value::Undefined);
        let mut timestamp: Value = self.safe_value(entry.clone(), Value::from("timestamp"), Value::Undefined);
        let mut currency_id: Value = self.safe_string(entry.clone(), Value::from("currency"), Value::Undefined);
        let mut code: Value = Luno::safe_currency_code(self, currency_id.clone(), currency.clone());
        let mut available_delta: Value = Luno::safe_number(self, entry.clone(), Value::from("available_delta"), Value::Undefined);
        let mut balance_delta: Value = Luno::safe_number(self, entry.clone(), Value::from("balance_delta"), Value::Undefined);
        let mut after: Value = Luno::safe_number(self, entry.clone(), Value::from("balance"), Value::Undefined);
        let mut comment: Value = self.safe_string(entry.clone(), Value::from("description"), Value::Undefined);
        let mut before: Value = after.clone();
        let mut amount: Value = Value::from(0);
        let mut result: Value = Luno::parse_ledger_comment(self, comment.clone());
        let mut r#type: Value = result.get(Value::from("type"));
        let mut reference_id: Value = result.get(Value::from("referenceId"));
        let mut direction: Value = Value::Undefined;
        let mut status: Value = Value::Undefined;
        if balance_delta.clone() != Value::from(0) {
            before = after.clone() - balance_delta.clone();
            // TODO: float precision
            status = Value::from("ok");
            amount = Math::abs(balance_delta.clone());
        } else if available_delta.clone() < Value::from(0) {
            status = Value::from("pending");
            amount = Math::abs(available_delta.clone());
        } else if available_delta.clone() > Value::from(0) {
            status = Value::from("canceled");
            amount = Math::abs(available_delta.clone());
        };
        if balance_delta.clone() > Value::from(0) || available_delta.clone() > Value::from(0) {
            direction = Value::from("in");
        } else if balance_delta.clone() < Value::from(0) || available_delta.clone() < Value::from(0) {
            direction = Value::from("out");
        };
        return Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "direction": direction,
            "account": account_id,
            "referenceId": reference_id,
            "referenceAccount": Value::Undefined,
            "type": r#type,
            "currency": code,
            "amount": amount,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "before": before,
            "after": after,
            "status": status,
            "fee": Value::Undefined,
            "info": entry
        }))).unwrap());
    }

    fn sign(&mut self, mut path: Value, mut api: Value, mut method: Value, mut params: Value, mut headers: Value, mut body: Value) -> Value {
        api = api.or_default(Value::from("public"));
        method = method.or_default(Value::from("GET"));
        params = params.or_default(Value::new_object());
        let mut url: Value = self.get("urls".into()).get(Value::from("api")).get(api.clone()) + Value::from("/") + self.get("version".into()) + Value::from("/") + self.implode_params(path.clone(), params.clone());
        let mut query: Value = self.omit(params.clone(), self.extract_params(path.clone()));
        if Object::keys(query.clone()).len() > 0 {
            url = url +  Value::from("?") + self.urlencode(query.clone());
        };
        if api.clone() == Value::from("private") {
            Luno::check_required_credentials(self, Value::Undefined);
            let mut auth: Value = self.string_to_base64(self.get("apiKey".into()) + Value::from(":") + self.get("secret".into()));
            headers = Value::Json(normalize(&Value::Json(json!({
                "Authorization": Value::from("Basic ") + self.decode(auth.clone())
            }))).unwrap());
        };
        return Value::Json(normalize(&Value::Json(json!({
            "url": url,
            "method": method,
            "body": body,
            "headers": headers
        }))).unwrap());
    }

