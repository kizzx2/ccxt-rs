        let mut amount: Value = parse_float(transaction.get(Value::from("amount")).get(Value::from(0)));
        let mut fee: Value = parse_float(transaction.get(Value::from("fee")).get(Value::from(0)));
        let mut fee_currency: Value = transaction.get(Value::from("fee")).get(Value::from(1));
        let mut status: Value = Buda::parse_transaction_status(self, self.safe_string(transaction.clone(), Value::from("state"), Value::Undefined));
