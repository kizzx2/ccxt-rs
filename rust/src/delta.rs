    fn parse_balance(&self, mut response: Value) -> Value {
        let mut balances: Value = self.safe_value(response.clone(), Value::from("result"), Value::new_array());
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "info": response
        }))).unwrap());
        let mut currencies_by_numeric_id: Value = self.safe_value(self.get("options".into()), Value::from("currenciesByNumericId"), Value::new_object());
        let mut i: usize = 0;
        while i < balances.len() {
            let mut balance: Value = balances.get(i.into());
            let mut currency_id: Value = self.safe_string(balance.clone(), Value::from("asset_id"), Value::Undefined);
            let mut currency: Value = self.safe_value(currencies_by_numeric_id.clone(), currency_id.clone(), Value::Undefined);
            let mut code: Value = if currency.clone().is_nullish() { currency_id.clone() } else { currency.get(Value::from("code")) };
            let mut account: Value = Delta::account(self);
            account.set("total".into(), self.safe_string(balance.clone(), Value::from("balance"), Value::Undefined));
            account.set("free".into(), self.safe_string(balance.clone(), Value::from("available_balance"), Value::Undefined));
            result.set(code.clone(), account.clone());
            i += 1;
        };
        return Delta::safe_balance(self, result.clone());
    }

