    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // public fetchTrades
        //
        //     {
        //         "buyer_role":"maker",
        //         "price":"15896.5",
        //         "seller_role":"taker",
        //         "size":241,
        //         "symbol":"BTCUSDT",
        //         "timestamp":1605376684714595
        //     }
        //
        // private fetchMyTrades
        //
        //     {
        //         "commission":"0.008335000000000000",
        //         "created_at":"2020-11-16T19:07:19Z",
        //         "fill_type":"normal",
        //         "id":"e7ff05c233a74245b72381f8dd91d1ce",
        //         "meta_data":{
        //             "effective_commission_rate":"0.0005",
        //             "order_price":"16249",
        //             "order_size":1,
        //             "order_type":"market_order",
        //             "order_unfilled_size":0,
        //             "trading_fee_credits_used":"0"
        //         },
        //         "order_id":"152999629",
        //         "price":"16669",
        //         "product":{
        //             "contract_type":"perpetual_futures",
        //             "contract_unit_currency":"BTC",
        //             "contract_value":"0.001",
        //             "id":139,
        //             "notional_type":"vanilla",
        //             "quoting_asset":{"minimum_precision":2,"precision":6,"symbol":"USDT"},
        //             "settling_asset":{"minimum_precision":2,"precision":6,"symbol":"USDT"},
        //             "symbol":"BTCUSDT",
        //             "tick_size":"0.5",
        //             "underlying_asset":{"minimum_precision":4,"precision":8,"symbol":"BTC"}
        //         },
        //         "product_id":139,
        //         "role":"taker",
        //         "side":"sell",
        //         "size":1
        //     }
        //
        let mut id: Value = self.safe_string(trade.clone(), Value::from("id"), Value::Undefined);
        let mut order_id: Value = self.safe_string(trade.clone(), Value::from("order_id"), Value::Undefined);
        let mut timestamp: Value = self.parse8601(self.safe_string(trade.clone(), Value::from("created_at"), Value::Undefined));
        timestamp = self.safe_integer_product(trade.clone(), Value::from("timestamp"), Value::from(0.001), timestamp.clone());
        let mut price_string: Value = self.safe_string(trade.clone(), Value::from("price"), Value::Undefined);
        let mut amount_string: Value = self.safe_string(trade.clone(), Value::from("size"), Value::Undefined);
        let mut product: Value = self.safe_value(trade.clone(), Value::from("product"), Value::new_object());
        let mut market_id: Value = self.safe_string(product.clone(), Value::from("symbol"), Value::Undefined);
        let mut symbol: Value = Delta::safe_symbol(self, market_id.clone(), market.clone(), Value::Undefined);
        let mut seller_role: Value = self.safe_string(trade.clone(), Value::from("seller_role"), Value::Undefined);
        let mut side: Value = self.safe_string(trade.clone(), Value::from("side"), Value::Undefined);
        if side.clone().is_nullish() {
            if seller_role.clone() == Value::from("taker") {
                side = Value::from("sell");
            } else if seller_role.clone() == Value::from("maker") {
                side = Value::from("buy");
            };
        };
        let mut taker_or_maker: Value = self.safe_string(trade.clone(), Value::from("role"), Value::Undefined);
        let mut meta_data: Value = self.safe_value(trade.clone(), Value::from("meta_data"), Value::new_object());
        let mut r#type: Value = self.safe_string(meta_data.clone(), Value::from("order_type"), Value::Undefined);
        if r#type.clone().is_nonnullish() {
            r#type = r#type.replace(Value::from("_order"), Value::from(""));
        };
        let mut fee_cost_string: Value = self.safe_string(trade.clone(), Value::from("commission"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        if fee_cost_string.clone().is_nonnullish() {
            let mut settling_asset: Value = self.safe_value(product.clone(), Value::from("settling_asset"), Value::new_object());
            let mut fee_currency_id: Value = self.safe_string(settling_asset.clone(), Value::from("symbol"), Value::Undefined);
            let mut fee_currency_code: Value = Delta::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost_string,
                "currency": fee_currency_code
            }))).unwrap());
        };
        return Delta::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "order": order_id,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "type": r#type,
            "side": side,
            "price": price_string,
            "amount": amount_string,
            "cost": Value::Undefined,
            "takerOrMaker": taker_or_maker,
            "fee": fee,
            "info": trade
        }))).unwrap()), market.clone());
    }

