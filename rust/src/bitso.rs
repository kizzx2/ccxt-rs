    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // fetchTrades (public)
        //
        //      {
        //          "book": "btc_usdt",
        //          "created_at": "2021-11-24T12:14:53+0000",
        //          "amount": "0.00026562",
        //          "maker_side": "sell",
        //          "price": "56471.55",
        //          "tid": "52557338"
        //      }
        //
        // fetchMyTrades (private)
        //
        //      {
        //          "book": "btc_usdt",
        //          "created_at": "2021-11-24T12:31:03+0000",
        //          "minor": "11.30356000",
        //          "major": "-0.00020000",
        //          "fees_amount": "0.01119052",
        //          "fees_currency": "usdt",
        //          "minor_currency": "usdt",
        //          "major_currency": "btc",
        //          "oid": "djTzMIWx2Vi3iMjl",
        //          "tid": "52559051",
        //          "price": "56517.80",
        //          "side": "sell",
        //          "maker_side": "buy"
        //      }
        //
        // fetchOrderTrades (private)
        //
        //      {
        //          "book": "btc_usdt",
        //          "created_at": "2021-11-24T12:30:52+0000",
        //          "minor": "-11.33047916",
        //          "major": "0.00020020",
        //          "fees_amount": "0.00000020",
        //          "fees_currency": "btc",
        //          "minor_currency": "usdt",
        //          "major_currency": "btc",
        //          "oid": "O0D2zcljjjQF5xlG",
        //          "tid": "52559030",
        //          "price": "56595.80",
        //          "side": "buy",
        //          "maker_side": "sell"
        //      }
        //
        let mut timestamp: Value = self.parse8601(self.safe_string(trade.clone(), Value::from("created_at"), Value::Undefined));
        let mut market_id: Value = self.safe_string(trade.clone(), Value::from("book"), Value::Undefined);
        let mut symbol: Value = Bitso::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut side: Value = self.safe_string_2(trade.clone(), Value::from("side"), Value::from("maker_side"), Value::Undefined);
        let mut maker_side: Value = self.safe_string(trade.clone(), Value::from("maker_side"), Value::Undefined);
        let mut taker_or_maker: Value = Value::Undefined;
        if side.clone() == maker_side.clone() {
            taker_or_maker = Value::from("maker");
        } else {
            taker_or_maker = Value::from("taker");
        };
        let mut amount: Value = self.safe_string_2(trade.clone(), Value::from("amount"), Value::from("major"), Value::Undefined);
        if amount.clone().is_nonnullish() {
            amount = Precise::string_abs(amount.clone());
        };
        let mut fee: Value = Value::Undefined;
        let mut fee_cost: Value = self.safe_string(trade.clone(), Value::from("fees_amount"), Value::Undefined);
        if fee_cost.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string(trade.clone(), Value::from("fees_currency"), Value::Undefined);
            let mut fee_currency: Value = Bitso::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost,
                "currency": fee_currency
            }))).unwrap());
        };
        let mut cost: Value = self.safe_string(trade.clone(), Value::from("minor"), Value::Undefined);
        if cost.clone().is_nonnullish() {
            cost = Precise::string_abs(cost.clone());
        };
        let mut price: Value = self.safe_string(trade.clone(), Value::from("price"), Value::Undefined);
        let mut order_id: Value = self.safe_string(trade.clone(), Value::from("oid"), Value::Undefined);
        let mut id: Value = self.safe_string(trade.clone(), Value::from("tid"), Value::Undefined);
        return Bitso::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "info": trade,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "order": order_id,
            "type": Value::Undefined,
            "side": side,
            "takerOrMaker": taker_or_maker,
            "price": price,
            "amount": amount,
            "cost": cost,
            "fee": fee
        }))).unwrap()), market.clone());
    }

