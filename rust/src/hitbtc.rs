    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // createMarketOrder
        //
        //     {
        //         clientOrderId: "fe36aa5e190149bf9985fb673bbb2ea0",
        //         createdAt: "2018-10-25T16:41:44.780Z",
        //         cumQuantity: "1",
        //         id: "66799540063",
        //         quantity: "1",
        //         side: "sell",
        //         status: "filled",
        //         symbol: "XRPUSDT",
        //         timeInForce: "FOK",
        //         tradesReport: [
        //             {
        //                 fee: "0.0004644",
        //                 id:  386394956,
        //                 price: "0.4644",
        //                 quantity: "1",
        //                 timestamp: "2018-10-25T16:41:44.780Z"
        //             }
        //         ],
        //         type: "market",
        //         updatedAt: "2018-10-25T16:41:44.780Z"
        //     }
        //
        //     {
        //         "id": 119499457455,
        //         "clientOrderId": "87baab109d58401b9202fa0749cb8288",
        //         "symbol": "ETHUSD",
        //         "side": "buy",
        //         "status": "filled",
        //         "type": "market",
        //         "timeInForce": "FOK",
        //         "quantity": "0.0007",
        //         "price": "181.487",
        //         "avgPrice": "164.989",
        //         "cumQuantity": "0.0007",
        //         "createdAt": "2019-04-17T13:27:38.062Z",
        //         "updatedAt": "2019-04-17T13:27:38.062Z"
        //     }
        //
        let mut created: Value = self.parse8601(self.safe_string(order.clone(), Value::from("createdAt"), Value::Undefined));
        let mut updated: Value = self.parse8601(self.safe_string(order.clone(), Value::from("updatedAt"), Value::Undefined));
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("symbol"), Value::Undefined);
        market = Hitbtc::safe_market(self, market_id.clone(), market.clone(), Value::Undefined);
        let mut symbol: Value = market.get(Value::from("symbol"));
        let mut amount: Value = self.safe_string(order.clone(), Value::from("quantity"), Value::Undefined);
        let mut filled: Value = self.safe_string(order.clone(), Value::from("cumQuantity"), Value::Undefined);
        let mut status: Value = Hitbtc::parse_order_status(self, self.safe_string(order.clone(), Value::from("status"), Value::Undefined));
        // we use clientOrderId as the order id with this exchange intentionally
        // because most of their endpoints will require clientOrderId
        // explained here: https://github.com/ccxt/ccxt/issues/5674
        let mut id: Value = self.safe_string(order.clone(), Value::from("clientOrderId"), Value::Undefined);
        let mut client_order_id: Value = id.clone();
        let mut price: Value = self.safe_string(order.clone(), Value::from("price"), Value::Undefined);
        let mut r#type: Value = self.safe_string(order.clone(), Value::from("type"), Value::Undefined);
        let mut side: Value = self.safe_string(order.clone(), Value::from("side"), Value::Undefined);
        let mut trades: Value = self.safe_value(order.clone(), Value::from("tradesReport"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        let mut average: Value = self.safe_string(order.clone(), Value::from("avgPrice"), Value::Undefined);
        let mut time_in_force: Value = self.safe_string(order.clone(), Value::from("timeInForce"), Value::Undefined);
        return Hitbtc::safe_order(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": client_order_id,
            "timestamp": created,
            "datetime": self.iso8601(created.clone()),
            "lastTradeTimestamp": updated,
            "status": status,
            "symbol": symbol,
            "type": r#type,
            "timeInForce": time_in_force,
            "side": side,
            "price": price,
            "stopPrice": Value::Undefined,
            "average": average,
            "amount": amount,
            "cost": Value::Undefined,
            "filled": filled,
            "remaining": Value::Undefined,
            "fee": fee,
            "trades": trades,
            "info": order
        }))).unwrap()), market.clone());
    }

