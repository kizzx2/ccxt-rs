    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // fetchClosedOrders / fetchOrder
        //     {
        //       "id": "923763073",
        //       "date": "1635451090368",
        //       "type": "sell",
        //       "pair": "XRP_USDT",
        //       "price": "1.00000000",
        //       "amount": "0.00000000",
        //       "status": "3",
        //       "amount_filled": "10.00000000",
        //       "amount_original": "10.0"
        //       "trades": [],
        //     }
        //
        // createOrder
        //     {
        //       "success": true,
        //       "date": "1635451754.497541",
        //       "type": "sell",
        //       "oid": "923776755",
        //       "price": "1.0",
        //       "amount": "10.0",
        //       "amount_filled": "0.0",
        //       "amount_original": "10.0",
        //       "trades": []
        //     }
        //
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("pair"), Value::Undefined);
        market = Btcalpha::safe_market(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut symbol: Value = market.get(Value::from("symbol"));
        let mut success: Value = self.safe_value(order.clone(), Value::from("success"), false.into());
        let mut timestamp: Value = Value::Undefined;
        if success.is_truthy() {
            timestamp = self.safe_timestamp(order.clone(), Value::from("date"), Value::Undefined);
        } else {
            timestamp = self.safe_integer(order.clone(), Value::from("date"), Value::Undefined);
        };
        let mut price: Value = self.safe_string(order.clone(), Value::from("price"), Value::Undefined);
        let mut remaining: Value = self.safe_string(order.clone(), Value::from("amount"), Value::Undefined);
        let mut filled: Value = self.safe_string(order.clone(), Value::from("amount_filled"), Value::Undefined);
        let mut amount: Value = self.safe_string(order.clone(), Value::from("amount_original"), Value::Undefined);
        let mut status: Value = Btcalpha::parse_order_status(self, self.safe_string(order.clone(), Value::from("status"), Value::Undefined));
        let mut id: Value = self.safe_string_2(order.clone(), Value::from("oid"), Value::from("id"), Value::Undefined);
        let mut trades: Value = self.safe_value(order.clone(), Value::from("trades"), Value::Undefined);
        let mut side: Value = self.safe_string_2(order.clone(), Value::from("my_side"), Value::from("type"), Value::Undefined);
        return Btcalpha::safe_order(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": Value::Undefined,
            "datetime": self.iso8601(timestamp.clone()),
            "timestamp": timestamp,
            "status": status,
            "symbol": symbol,
            "type": "limit",
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price,
            "stopPrice": Value::Undefined,
            "cost": Value::Undefined,
            "amount": amount,
            "filled": filled,
            "remaining": remaining,
            "trades": trades,
            "fee": Value::Undefined,
            "info": order,
            "lastTradeTimestamp": Value::Undefined,
            "average": Value::Undefined
        }))).unwrap()), market.clone());
    }

