    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        //
        // withdraw()
        //
        //     {
        //         "withdrawalRequestId": 47383243,
        //         "status": 1,
        //         "statusMessage": "Pending confirmation"
        //     }
        //
        // fetchWithdrawals
        //
        //     {
        //         "withdrawalRequestId": 47383243,
        //         "externalId": "...",    // external ID submitted by the client when creating the request
        //         "status": 1,
        //         "statusMessage": "Pending confirmation",
        //         "amount": "10.2",
        //         "currency": "BTC",
        //         "lastUpdated": <UNIX nanoseconds>,
        //         "blockchain": "Bitcoin",
        //         "address": "mu5GceHFAG38mGRYCFqafe5ZiNKLX3rKk9",
        //         "txId": "0xfbb1b73c4f0bda4f67dca266ce6ef42f520fbb98"
        //     }
        //
        // fetchDeposits
        //
        //     {
        //         "currency": "BTC",
        //         "amount": "1.2",
        //         "status": 1,
        //         "statusMessage": "Processing",
        //         "blockchain": "Bitcoin",
        //         "txId": "0xfbb1b73c4f0bda4f67dca266ce6ef42f520fbb98",
        //         "address": "mu5GceHFAG38mGRYCFqafe5ZiNKLX3rKk9",
        //         "lastUpdated": <UNIX nanoseconds>
        //         "confirmations": 2,
        //         "requiredConfirmations": 6
        //     }
        //
        let mut id: Value = self.safe_string(transaction.clone(), Value::from("withdrawalRequestId"), Value::Undefined);
        let mut r#type: Value = if id.clone().is_nullish() { Value::from("deposit") } else { Value::from("withdrawal") };
        let mut updated: Value = self.safe_integer(transaction.clone(), Value::from("lastUpdated"), Value::Undefined);
        if updated.clone().is_nonnullish() {
            updated = parse_int(updated.clone() / Value::from(1000000));
        };
        let mut timestamp: Value = Value::Undefined;
        let mut txid: Value = self.safe_string(transaction.clone(), Value::from("txId"), Value::Undefined);
        let mut currency_id: Value = self.safe_string(transaction.clone(), Value::from("currency"), Value::Undefined);
        let mut code: Value = Xena::safe_currency_code(self, currency_id.clone(), currency.clone());
        let mut address: Value = self.safe_string(transaction.clone(), Value::from("address"), Value::Undefined);
        let mut address_from: Value = Value::Undefined;
        let mut address_to: Value = address.clone();
        let mut amount: Value = Xena::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined);
        let mut status: Value = Xena::parse_transaction_status(self, self.safe_string(transaction.clone(), Value::from("status"), Value::Undefined));
        let mut fee: Value = Value::Undefined;
        let mut network: Value = self.safe_string(transaction.clone(), Value::from("blockchain"), Value::Undefined);
        return Value::Json(normalize(&Value::Json(json!({
            "info": transaction,
            "id": id,
            "txid": txid,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "network": network,
            "addressFrom": address_from,
            "addressTo": address_to,
            "address": address,
            "tagFrom": Value::Undefined,
            "tagTo": Value::Undefined,
            "tag": Value::Undefined,
            "type": r#type,
            "amount": amount,
            "currency": code,
            "status": status,
            "updated": updated,
            "fee": fee
        }))).unwrap());
    }

