    fn parse_ledger_entry(&self, mut item: Value, mut currency: Value) -> Value {
        //
        //     {
        //         "accountId":8263118,
        //         "ts":1551974415000000000,
        //         "amount":"-1",
        //         "currency":"BTC",
        //         "kind":"internal withdrawal",
        //         "commission":"0",
        //         "id":96
        //     }
        //
        let mut id: Value = self.safe_string(item.clone(), Value::from("id"), Value::Undefined);
        let mut direction: Value = Value::Undefined;
        let mut account: Value = self.safe_string(item.clone(), Value::from("accountId"), Value::Undefined);
        let mut reference_id: Value = Value::Undefined;
        let mut reference_account: Value = Value::Undefined;
        let mut r#type: Value = Xena::parse_ledger_entry_type(self, self.safe_string(item.clone(), Value::from("kind"), Value::Undefined));
        let mut code: Value = Xena::safe_currency_code(self, self.safe_string(item.clone(), Value::from("currency"), Value::Undefined), currency.clone());
        let mut amount: Value = Xena::safe_number(self, item.clone(), Value::from("amount"), Value::Undefined);
        if amount.clone() < Value::from(0) {
            direction = Value::from("out");
            amount = Math::abs(amount.clone());
        } else {
            direction = Value::from("in");
        };
        let mut timestamp: Value = self.safe_integer(item.clone(), Value::from("ts"), Value::Undefined);
        if timestamp.clone().is_nonnullish() {
            timestamp = parse_int(timestamp.clone() / Value::from(1000000));
        };
        let mut fee: Value = Value::Json(normalize(&Value::Json(json!({
            "cost": Xena::safe_number(self, item.clone(), Value::from("commission"), Value::Undefined),
            "currency": code
        }))).unwrap());
        let mut before: Value = Value::Undefined;
        let mut after: Value = Xena::safe_number(self, item.clone(), Value::from("balance"), Value::Undefined);
        let mut status: Value = Value::from("ok");
        return Value::Json(normalize(&Value::Json(json!({
            "info": item,
            "id": id,
            "direction": direction,
            "account": account,
            "referenceId": reference_id,
            "referenceAccount": reference_account,
            "type": r#type,
            "currency": code,
            "amount": amount,
            "before": before,
            "after": after,
            "status": status,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "fee": fee
        }))).unwrap());
    }

