    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // createOrder
        //
        //     {
        //         "msgType":"8",
        //         "account":1012838720,
        //         "clOrdId":"XAq0pRQ1g",
        //         "orderId":"64d7a06a-27e5-422e-99d9-3cadc04f5a35",
        //         "symbol":"XBTUSD",
        //         "ordType":"2",
        //         "price":"9000",
        //         "transactTime":1593778763271127920,
        //         "execId":"ff5fb8153652f0516bf07b6979255bed053c84b9",
        //         "execType":"I",
        //         "ordStatus":"0",
        //         "side":"1",
        //         "orderQty":"1",
        //         "leavesQty":"1",
        //         "cumQty":"0",
        //         "positionEffect":"O",
        //         "marginAmt":"0.00000556",
        //         "marginAmtType":"11"
        //     }
        //
        let mut id: Value = self.safe_string(order.clone(), Value::from("orderId"), Value::Undefined);
        let mut client_order_id: Value = self.safe_string(order.clone(), Value::from("clOrdId"), Value::Undefined);
        let mut transact_time: Value = self.safe_integer(order.clone(), Value::from("transactTime"), Value::Undefined);
        let mut timestamp: Value = parse_int(transact_time.clone() / Value::from(1000000));
        let mut status: Value = Xena::parse_order_status(self, self.safe_string(order.clone(), Value::from("ordStatus"), Value::Undefined));
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("symbol"), Value::Undefined);
        let mut symbol: Value = Xena::safe_symbol(self, market_id.clone(), market.clone(), Value::Undefined);
        let mut price: Value = self.safe_string(order.clone(), Value::from("price"), Value::Undefined);
        let mut amount: Value = self.safe_string(order.clone(), Value::from("orderQty"), Value::Undefined);
        let mut filled: Value = self.safe_string(order.clone(), Value::from("cumQty"), Value::Undefined);
        let mut remaining: Value = self.safe_string(order.clone(), Value::from("leavesQty"), Value::Undefined);
        let mut side: Value = self.safe_string(order.clone(), Value::from("side"), Value::Undefined);
        if side.clone() == Value::from("1") {
            side = Value::from("buy");
        } else if side.clone() == Value::from("2") {
            side = Value::from("sell");
        };
        let mut r#type: Value = self.safe_string(order.clone(), Value::from("ordType"), Value::Undefined);
        if r#type.clone() == Value::from("1") {
            r#type = Value::from("market");
        } else if r#type.clone() == Value::from("2") {
            r#type = Value::from("limit");
        } else if r#type.clone() == Value::from("3") {
            r#type = Value::from("stop");
        } else if r#type.clone() == Value::from("4") {
            r#type = Value::from("stop-limit");
        };
        return Xena::safe_order(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": client_order_id,
            "info": order,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "lastTradeTimestamp": Value::Undefined,
            "symbol": symbol,
            "type": r#type,
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price,
            "stopPrice": Value::Undefined,
            "amount": amount,
            "cost": Value::Undefined,
            "average": Value::Undefined,
            "filled": filled,
            "remaining": remaining,
            "status": status,
            "fee": Value::Undefined,
            "trades": Value::Undefined
        }))).unwrap()), market.clone());
    }

    /// Returns an [order structure](https://docs.ccxt.com/en/latest/manual.html#order-structure)
    ///
    /// Create a trade order
    ///
    /// # Arguments
    ///
    /// * `symbol` {string} - unified symbol of the market to create an order in
    /// * `type` {string} - 'market' or 'limit'
    /// * `side` {string} - 'buy' or 'sell'
    /// * `amount` {float} - how much of currency you want to trade in units of base currency
    /// * `price` {float|undefined} - the price at which the order is to be fullfilled, in units of the quote currency, ignored in market orders
    /// * `params` {object} - extra parameters specific to the xena api endpoint
    async fn create_order(&mut self, mut symbol: Value, mut r#type: Value, mut side: Value, mut amount: Value, mut price: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        Xena::load_markets(self, Value::Undefined, Value::Undefined).await;
        Xena::load_accounts(self, Value::Undefined, Value::Undefined).await;
        let mut account_id: Value = Xena::get_account_id(self, params.clone()).await;
        let mut order_types: Value = Value::Json(normalize(&Value::Json(json!({
            "market": "1",
            "limit": "2",
            "stop": "3",
            "stop-limit": "4"
        }))).unwrap());
        let mut order_type: Value = self.safe_string(order_types.clone(), r#type.clone(), Value::Undefined);
        if order_type.clone().is_nullish() {
            panic!(r###"InvalidOrder::new(self.get("id".into()) + Value::from(" createOrder() does not support order type ") + r#type.clone() + Value::from(", supported order types are market, limit, stop, stop-limit"))"###);
        };
        let mut order_sides: Value = Value::Json(normalize(&Value::Json(json!({
            "buy": "1",
            "sell": "2"
        }))).unwrap());
        let mut order_side: Value = self.safe_string(order_sides.clone(), side.clone(), Value::Undefined);
        if order_side.clone().is_nullish() {
            panic!(r###"InvalidOrder::new(self.get("id".into()) + Value::from(" createOrder() does not support order side ") + side.clone() + Value::from(", supported order sides are buy, sell"))"###);
        };
        let mut market: Value = Xena::market(self, symbol.clone());
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "account": parse_int(account_id.clone()),
            "symbol": market.get(Value::from("id")),
            "ordType": order_type,
            "side": order_side,
            "orderQty": Xena::amount_to_precision(self, symbol.clone(), amount.clone()),
            "transactTime": self.milliseconds() * Value::from(1000000)
        }))).unwrap());
        // 'clOrdId': this.uuid (), // required
        // 'price': this.priceToPrecision (symbol, price), // required for limit and stop-limit orders
        // 'stopPx': this.priceToPrecision (symbol, stopPx), // required for stop and stop-limit orders
        // 'timeInForce': '1', // default '1' = GoodTillCancelled, '3' = ImmediateOrCancel, '4' = FillOrKill
        // 'execInst': '0',
        //     '0' = StayOnOfferSide, maker only, reject instead of aggressive execution
        //     '9' = PegToOfferSide, maker only, best available level instead of aggressive execution
        //     'o' = CancelOnConnectionLoss
        // 'positionID': 1013838923, // required when positionEffect == 'C' with hedged accounting
        // 'positionEffect': 'O', // 'C' = Close, 'O' = Open, send C along with the positionID if the order must close a position with hedged accounting mode
        // 'text': 'comment', // optional
        // 'grpID': 'group-identifier', // group identifier for cancel on disconnect orders
        if r#type.clone() == Value::from("limit") || r#type.clone() == Value::from("stop-limit") {
            if price.clone().is_nullish() {
                panic!(r###"InvalidOrder::new(self.get("id".into()) + Value::from(" createOrder() requires a price argument for order type ") + r#type.clone())"###);
            };
            request.set("price".into(), Xena::price_to_precision(self, symbol.clone(), price.clone()));
        };
        if r#type.clone() == Value::from("stop") || r#type.clone() == Value::from("stop-limit") {
            let mut stop_px: Value = Xena::safe_number(self, params.clone(), Value::from("stopPx"), Value::Undefined);
            if stop_px.clone().is_nullish() {
                panic!(r###"InvalidOrder::new(self.get("id".into()) + Value::from(" createOrder() requires a stopPx param for order type ") + r#type.clone())"###);
            };
            request.set("stopPx".into(), Xena::price_to_precision(self, symbol.clone(), stop_px.clone()));
            params = self.omit(params.clone(), Value::from("stopPx"));
        };
        let mut client_order_id: Value = self.safe_string_2(params.clone(), Value::from("clientOrderId"), Value::from("clOrdId"), self.uuid(Value::Undefined));
        if client_order_id.clone().is_nonnullish() {
            request.set("clOrdId".into(), client_order_id.clone());
            params = self.omit(params.clone(), Value::Json(serde_json::Value::Array(vec![Value::from("clientOrderId").into(), Value::from("clOrdId").into()])));
        };
        let mut response: Value = Xena::dispatch(self, "privatePostTradingOrderNew".into(), extend_2(request.clone(), params.clone()), Value::Undefined).await;
        //
        //     {
        //         "msgType":"8",
        //         "account":1012838720,
        //         "clOrdId":"XAq0pRQ1g",
        //         "orderId":"64d7a06a-27e5-422e-99d9-3cadc04f5a35",
        //         "symbol":"XBTUSD",
        //         "ordType":"2",
        //         "price":"9000",
        //         "transactTime":1593778763271127920,
        //         "execId":"ff5fb8153652f0516bf07b6979255bed053c84b9",
        //         "execType":"I",
        //         "ordStatus":"0",
        //         "side":"1",
        //         "orderQty":"1",
        //         "leavesQty":"1",
        //         "cumQty":"0",
        //         "positionEffect":"O",
        //         "marginAmt":"0.00000556",
        //         "marginAmtType":"11"
        //     }
        //
        return Xena::parse_order(self, response.clone(), market.clone());
    }

    async fn edit_order(&mut self, mut id: Value, mut symbol: Value, mut r#type: Value, mut side: Value, mut amount: Value, mut price: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        if symbol.clone().is_nullish() {
            panic!(r###"ArgumentsRequired::new(self.get("id".into()) + Value::from(" editOrder() requires a symbol argument"))"###);
        };
        Xena::load_markets(self, Value::Undefined, Value::Undefined).await;
        Xena::load_accounts(self, Value::Undefined, Value::Undefined).await;
        let mut account_id: Value = Xena::get_account_id(self, params.clone()).await;
        let mut market: Value = Xena::market(self, symbol.clone());
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "account": parse_int(account_id.clone()),
            "clOrdId": self.uuid(Value::Undefined),
            "symbol": market.get(Value::from("id")),
            "transactTime": self.milliseconds() * Value::from(1000000)
        }))).unwrap());
        // 'origClOrdId': this.uuid (), // one of orderId or origClOrdId is required
        // 'orderId': id,
        // 'side': '1', // 1 = buy, 2 = sell
        // 'execInst': '0',
        //     '0' = StayOnOfferSide, maker only, reject instead of aggressive execution
        //     '9' = PegToOfferSide, maker only, best available level instead of aggressive execution
        //     'o' = CancelOnConnectionLoss
        // 'orderQty': 38 M decimal
        // 'price': this.priceToPrecision (symbol, price), // required for limit and stop-limit orders
        // 'stopPx': this.priceToPrecision (symbol, stopPx), // required for stop and stop-limit orders
        // 'capPrice': this.priceToPrecision (symbol, capPrice), // the price beyond which the order will not move for trailing stop and attempt-zero-loss
        // 'pegPriceType': '8', // '8' = TrailingStopPeg, identifies a trailing stop or an attempt-zero-loss order
        // 'pegOffsetType': '2', // '2' = BasisPoints, the unit of the distance to the stop price for a trailing stop or an attempt-zero-loss order
        // 'pegOffsetValue': 123, // distance to the trailing stop or attempt-zero-loss
        let mut client_order_id: Value = self.safe_string_2(params.clone(), Value::from("clientOrderId"), Value::from("origClOrdId"), Value::Undefined);
        if client_order_id.clone().is_nonnullish() {
            request.set("origClOrdId".into(), client_order_id.clone());
            params = self.omit(params.clone(), Value::Json(serde_json::Value::Array(vec![Value::from("clientOrderId").into(), Value::from("origClOrdId").into()])));
        } else {
            request.set("orderId".into(), id.clone());
        };
        if amount.clone().is_nonnullish() {
            request.set("orderQty".into(), Xena::amount_to_precision(self, symbol.clone(), amount.clone()));
        };
        if price.clone().is_nonnullish() {
            request.set("price".into(), Xena::price_to_precision(self, symbol.clone(), price.clone()));
        };
        let mut stop_px: Value = Xena::safe_number(self, params.clone(), Value::from("stopPx"), Value::Undefined);
        if stop_px.clone().is_nonnullish() {
            request.set("stopPx".into(), Xena::price_to_precision(self, symbol.clone(), stop_px.clone()));
            params = self.omit(params.clone(), Value::from("stopPx"));
        };
        let mut cap_price: Value = Xena::safe_number(self, params.clone(), Value::from("capPrice"), Value::Undefined);
        if cap_price.clone().is_nonnullish() {
            request.set("capPrice".into(), Xena::price_to_precision(self, symbol.clone(), cap_price.clone()));
            params = self.omit(params.clone(), Value::from("capPrice"));
        };
        let mut response: Value = Xena::dispatch(self, "privatePostTradingOrderReplace".into(), extend_2(request.clone(), params.clone()), Value::Undefined).await;
        return Xena::parse_order(self, response.clone(), market.clone());
    }

