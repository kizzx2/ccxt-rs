    fn parse_balance(&self, mut response: Value) -> Value {
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "info": response
        }))).unwrap());
        let mut timestamp: Value = Value::Undefined;
        let mut balances: Value = self.safe_value(response.clone(), Value::from("balances"), Value::new_array());
        let mut i: usize = 0;
        while i < balances.len() {
            let mut balance: Value = balances.get(i.into());
            let mut last_update_time: Value = self.safe_string(balance.clone(), Value::from("lastUpdateTime"), Value::Undefined);
            let mut last_updated: Value = last_update_time.slice(Value::from(0), Value::from(13));
            let mut current_timestamp: Value = parse_int(last_updated.clone());
            timestamp = if timestamp.clone().is_nullish() { current_timestamp.clone() } else { Math::max(timestamp.clone(), current_timestamp.clone()) };
            let mut currency_id: Value = self.safe_string(balance.clone(), Value::from("currency"), Value::Undefined);
            let mut code: Value = Xena::safe_currency_code(self, currency_id.clone(), Value::Undefined);
            let mut account: Value = Xena::account(self);
            account.set("free".into(), self.safe_string(balance.clone(), Value::from("available"), Value::Undefined));
            account.set("used".into(), self.safe_string(balance.clone(), Value::from("onHold"), Value::Undefined));
            result.set(code.clone(), account.clone());
            i += 1;
        };
        result.set("timestamp".into(), timestamp.clone());
        result.set("datetime".into(), self.iso8601(timestamp.clone()));
        return Xena::safe_balance(self, result.clone());
    }

