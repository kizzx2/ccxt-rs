    /// Returns an array of [ticker structures](https://docs.ccxt.com/en/latest/manual.html#ticker-structure)
    ///
    /// Fetches price tickers for multiple markets, statistical calculations with the information calculated over the past 24 hours each market
    ///
    /// # Arguments
    ///
    /// * `symbols` {[string]|undefined} - unified symbols of the markets to fetch the ticker for, all market tickers are returned if not assigned
    /// * `params` {object} - extra parameters specific to the coinbase api endpoint
    async fn fetch_tickers(&mut self, mut symbols: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        Coinbase::load_markets(self, Value::Undefined, Value::Undefined).await;
        symbols = Coinbase::market_symbols(self, symbols.clone());
        let mut request: Value = Value::new_object();
        // 'currency': 'USD',
        let mut response: Value = Coinbase::dispatch(self, "publicGetExchangeRates".into(), extend_2(request.clone(), params.clone()), Value::Undefined).await;
        //
        //     {
        //         "data":{
        //             "currency":"USD",
        //             "rates":{
        //                 "AED":"3.6731",
        //                 "AFN":"103.163942",
        //                 "ALL":"106.973038",
        //             }
        //         }
        //     }
        //
        let mut data: Value = self.safe_value(response.clone(), Value::from("data"), Value::new_object());
        let mut rates: Value = self.safe_value(data.clone(), Value::from("rates"), Value::new_object());
        let mut quote_id: Value = self.safe_string(data.clone(), Value::from("currency"), Value::Undefined);
        let mut result: Value = Value::new_object();
        let mut base_ids: Value = Object::keys(rates.clone());
        let mut delimiter: Value = Value::from("-");
        let mut i: usize = 0;
        while i < base_ids.len() {
            let mut base_id: Value = base_ids.get(i.into());
            let mut market_id: Value = base_id.clone() + delimiter.clone() + quote_id.clone();
            let mut market: Value = Coinbase::safe_market(self, market_id.clone(), Value::Undefined, delimiter.clone());
            let mut symbol: Value = market.get(Value::from("symbol"));
            result.set(symbol.clone(), Coinbase::parse_ticker(self, rates.get(base_id.clone()), market.clone()));
            i += 1;
        };
        return Coinbase::filter_by_array(self, result.clone(), Value::from("symbol"), symbols.clone(), Value::Undefined);
    }

    /// Returns a [ticker structure](https://docs.ccxt.com/en/latest/manual.html#ticker-structure)
    ///
    /// Fetches a price ticker, a statistical calculation with the information calculated over the past 24 hours for a specific market
    ///
    /// # Arguments
    ///
    /// * `symbol` {string} - unified symbol of the market to fetch the ticker for
    /// * `params` {object} - extra parameters specific to the coinbase api endpoint
    async fn fetch_ticker(&mut self, mut symbol: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        Coinbase::load_markets(self, Value::Undefined, Value::Undefined).await;
        let mut market: Value = Coinbase::market(self, symbol.clone());
        let mut request: Value = extend_2(Value::Json(normalize(&Value::Json(json!({
            "symbol": market.get(Value::from("id"))
        }))).unwrap()), params.clone());
        let mut spot: Value = Coinbase::dispatch(self, "publicGetPricesSymbolSpot".into(), request.clone(), Value::Undefined).await;
        //
        //     {"data":{"base":"BTC","currency":"USD","amount":"48691.23"}}
        //
        let mut buy: Value = Coinbase::dispatch(self, "publicGetPricesSymbolBuy".into(), request.clone(), Value::Undefined).await;
        //
        //     {"data":{"base":"BTC","currency":"USD","amount":"48691.23"}}
        //
        let mut sell: Value = Coinbase::dispatch(self, "publicGetPricesSymbolSell".into(), request.clone(), Value::Undefined).await;
        //
        //     {"data":{"base":"BTC","currency":"USD","amount":"48691.23"}}
        //
        return Coinbase::parse_ticker(self, Value::Json(serde_json::Value::Array(vec![spot.clone().into(), buy.clone().into(), sell.clone().into()])), market.clone());
    }

    fn parse_ticker(&self, mut ticker: Value, mut market: Value) -> Value {
        //
        // fetchTicker
        //
        //     [
        //         "48691.23", // spot
        //         "48691.23", // buy
        //         "48691.23",  // sell
        //     ]
        //
        // fetchTickers
        //
        //     "48691.23"
        //
        let mut symbol: Value = Coinbase::safe_symbol(self, Value::Undefined, market.clone(), Value::Undefined);
        let mut ask: Value = Value::Undefined;
        let mut bid: Value = Value::Undefined;
        let mut last: Value = Value::Undefined;
        let mut timestamp: Value = self.milliseconds();
        if ticker.typeof_() != Value::from("string") {
            let (mut spot, mut buy, mut sell) = shift_2(ticker.clone());
            let mut spot_data: Value = self.safe_value(spot.clone(), Value::from("data"), Value::new_object());
            let mut buy_data: Value = self.safe_value(buy.clone(), Value::from("data"), Value::new_object());
            let mut sell_data: Value = self.safe_value(sell.clone(), Value::from("data"), Value::new_object());
            last = self.safe_string(spot_data.clone(), Value::from("amount"), Value::Undefined);
            bid = self.safe_string(buy_data.clone(), Value::from("amount"), Value::Undefined);
            ask = self.safe_string(sell_data.clone(), Value::from("amount"), Value::Undefined);
        };
        return Coinbase::safe_ticker(self, Value::Json(normalize(&Value::Json(json!({
            "symbol": symbol,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "bid": bid,
            "ask": ask,
            "last": last,
            "high": Value::Undefined,
            "low": Value::Undefined,
            "bidVolume": Value::Undefined,
            "askVolume": Value::Undefined,
            "vwap": Value::Undefined,
            "open": Value::Undefined,
            "close": last,
            "previousClose": Value::Undefined,
            "change": Value::Undefined,
            "percentage": Value::Undefined,
            "average": Value::Undefined,
            "baseVolume": Value::Undefined,
            "quoteVolume": Value::Undefined,
            "info": ticker
        }))).unwrap()), market.clone());
    }

    fn parse_balance(&self, mut response: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        let mut balances: Value = self.safe_value(response.clone(), Value::from("data"), Value::new_array());
        let mut accounts: Value = self.safe_value(params.clone(), Value::from("type"), self.get("options".into()).get(Value::from("accounts")));
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "info": response
        }))).unwrap());
        let mut b: usize = 0;
        while b < balances.len() {
            let mut balance: Value = balances.get(b.into());
            let mut r#type: Value = self.safe_string(balance.clone(), Value::from("type"), Value::Undefined);
            if self.in_array(r#type.clone(), accounts.clone()).is_truthy() {
                let mut value: Value = self.safe_value(balance.clone(), Value::from("balance"), Value::Undefined);
                if value.clone().is_nonnullish() {
                    let mut currency_id: Value = self.safe_string(value.clone(), Value::from("currency"), Value::Undefined);
                    let mut code: Value = Coinbase::safe_currency_code(self, currency_id.clone(), Value::Undefined);
                    let mut total: Value = self.safe_string(value.clone(), Value::from("amount"), Value::Undefined);
                    let mut free: Value = total.clone();
                    let mut account: Value = self.safe_value(result.clone(), code.clone(), Value::Undefined);
                    if account.clone().is_nullish() {
                        account = Coinbase::account(self);
                        account.set("free".into(), free.clone());
                        account.set("total".into(), total.clone());
                    } else {
                        account.set("free".into(), Precise::string_add(account.get(Value::from("free")), total.clone()));
                        account.set("total".into(), Precise::string_add(account.get(Value::from("total")), total.clone()));
                    };
                    result.set(code.clone(), account.clone());
                };
            };
            b += 1;
        };
        return Coinbase::safe_balance(self, result.clone());
    }

    /// Returns a [balance structure](https://docs.ccxt.com/en/latest/manual.html?#balance-structure)
    ///
    /// Query for balance and get the amount of funds available for trading or funds locked in orders
    ///
    /// # Arguments
    ///
    /// * `params` {object} - extra parameters specific to the coinbase api endpoint
