    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        let mut id: Value = self.safe_string(transaction.clone(), Value::from("id"), Value::Undefined);
        let mut tx_hash: Value = self.safe_string(transaction.clone(), Value::from("blockchainTransactionHash"), Value::Undefined);
        let mut amount: Value = Currencycom::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined);
        let mut timestamp: Value = self.safe_integer(transaction.clone(), Value::from("timestamp"), Value::Undefined);
        let mut currency_id: Value = self.safe_string(transaction.clone(), Value::from("currency"), Value::Undefined);
        let mut code: Value = Currencycom::safe_currency_code(self, currency_id.clone(), currency.clone());
        let mut state: Value = Currencycom::parse_transaction_status(self, self.safe_string(transaction.clone(), Value::from("state"), Value::Undefined));
        let mut r#type: Value = Currencycom::parse_transaction_type(self, self.safe_string(transaction.clone(), Value::from("type"), Value::Undefined));
        let mut fee_cost: Value = self.safe_string(transaction.clone(), Value::from("commission"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        if fee_cost.clone().is_nonnullish() {
            fee = Value::Json(normalize(&Value::Json(json!({
                "currency": code,
                "cost": fee_cost
            }))).unwrap());
        };
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "txid": tx_hash,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "network": Value::Undefined,
            "addressFrom": Value::Undefined,
            "address": Value::Undefined,
            "addressTo": Value::Undefined,
            "tagFrom": Value::Undefined,
            "tag": Value::Undefined,
            "tagTo": Value::Undefined,
            "type": r#type,
            "amount": amount,
            "currency": code,
            "status": state,
            "updated": Value::Undefined,
            "comment": Value::Undefined,
            "fee": fee,
            "info": transaction
        }))).unwrap());
        return result.clone();
    }

