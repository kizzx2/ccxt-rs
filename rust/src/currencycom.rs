    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // fetchTrades (public aggregate trades)
        //
        //     {
        //         "a":"1658318071",    // Aggregate tradeId
        //         "p":"0.02476",       // Price
        //         "q":"0.0",           // Official doc says: "Quantity (should be ignored)"
        //         "T":"1591001423382", // Epoch timestamp in MS
        //         "m":false            // Was the buyer the maker
        //     }
        //
        // createOrder fills (private)
        //
        //     {
        //         "price": "9807.05",
        //         "qty": "0.01",
        //         "commission": "0",
        //         "commissionAsset": "dUSD"
        //     }
        //
        // fetchMyTrades
        //
        //     {
        //         "symbol": "DOGE/USD",
        //         "id": "116046000",
        //         "orderId": "00000000-0000-0000-0000-000006dbb8ad",
        //         "price": "0.14094",
        //         "qty": "40.0",
        //         "commission": "0.01",
        //         "commissionAsset": "USD",
        //         "time": "1645283022351",
        //         "buyer": false,
        //         "maker": false,
        //         "isBuyer": false,
        //         "isMaker": false
        //     }
        //
        let mut timestamp: Value = self.safe_integer_2(trade.clone(), Value::from("T"), Value::from("time"), Value::Undefined);
        let mut price_string: Value = self.safe_string_2(trade.clone(), Value::from("p"), Value::from("price"), Value::Undefined);
        let mut amount_string: Value = self.safe_string_2(trade.clone(), Value::from("q"), Value::from("qty"), Value::Undefined);
        let mut id: Value = self.safe_string_2(trade.clone(), Value::from("a"), Value::from("id"), Value::Undefined);
        let mut side: Value = Value::Undefined;
        let mut order_id: Value = self.safe_string(trade.clone(), Value::from("orderId"), Value::Undefined);
        let mut taker_or_maker: Value = Value::Undefined;
        if trade.contains_key(Value::from("m")) {
            side = if trade.get(Value::from("m")).is_truthy() { Value::from("sell") } else { Value::from("buy") };
            // this is reversed intentionally [TODO: needs reason to be mentioned]
            taker_or_maker = Value::from("taker");
        } else if trade.contains_key(Value::from("isBuyer")) {
            // in public trades, it's always taker
            side = if trade.get(Value::from("isBuyer")).is_truthy() { Value::from("buy") } else { Value::from("sell") };
            // this is a true side
            taker_or_maker = if trade.get(Value::from("isMaker")).is_truthy() { Value::from("maker") } else { Value::from("taker") };
        };
        let mut fee: Value = Value::Undefined;
        if trade.contains_key(Value::from("commission")) {
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": self.safe_string(trade.clone(), Value::from("commission"), Value::Undefined),
                "currency": Currencycom::safe_currency_code(self, self.safe_string(trade.clone(), Value::from("commissionAsset"), Value::Undefined), Value::Undefined)
            }))).unwrap());
        };
        let mut market_id: Value = self.safe_string(trade.clone(), Value::from("symbol"), Value::Undefined);
        let mut symbol: Value = Currencycom::safe_symbol(self, market_id.clone(), market.clone(), Value::Undefined);
        return Currencycom::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "order": order_id,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "type": Value::Undefined,
            "takerOrMaker": taker_or_maker,
            "side": side,
            "price": price_string,
            "amount": amount_string,
            "cost": Value::Undefined,
            "fee": fee,
            "info": trade
        }))).unwrap()), market.clone());
    }

