        let mut id: Value = self.safe_string_2(order.clone(), Value::from("data"), Value::from("id"), Value::Undefined);
        let mut status: Value = Value::Undefined;
        if id.clone().is_nullish() {
            let mut keys: Value = Object::keys(order.clone());
            id = self.safe_string(keys.clone(), Value::from(0), Value::Undefined);
            let mut state: Value = self.safe_string(order.clone(), id.clone(), Value::Undefined);
            if state.clone() == Value::from("success") {
                status = Value::from("canceled");
            };
        };
        let mut state: Value = self.safe_string(order.clone(), Value::from("state"), Value::Undefined);
        let mut timestamp: Value = self.safe_integer_2(order.clone(), Value::from("create_time"), Value::from("createTime"), Value::Undefined);
        let mut price: Value = self.safe_string(order.clone(), Value::from("price"), Value::Undefined);
        let mut amount: Value = self.safe_string_2(order.clone(), Value::from("quantity"), Value::from("vol"), Value::Undefined);
        let mut remaining: Value = self.safe_string(order.clone(), Value::from("remain_quantity"), Value::Undefined);
        let mut filled: Value = self.safe_string_2(order.clone(), Value::from("deal_quantity"), Value::from("dealVol"), Value::Undefined);
        let mut cost: Value = self.safe_string(order.clone(), Value::from("deal_amount"), Value::Undefined);
        let mut market_id: Value = self.safe_string(order.clone(), Value::from("symbol"), Value::Undefined);
        let mut symbol: Value = Mexc::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut side_check: Value = self.safe_integer(order.clone(), Value::from("side"), Value::Undefined);
        let mut side: Value = Value::Undefined;
        let mut bid_or_ask: Value = self.safe_string(order.clone(), Value::from("type"), Value::Undefined);
        if bid_or_ask.clone() == Value::from("BID") {
            side = Value::from("buy");
        } else if bid_or_ask.clone() == Value::from("ASK") {
            side = Value::from("sell");
        };
        if side_check.clone() == Value::from(1) {
            side = Value::from("open long");
        } else if side.clone() == Value::from(2) {
            side = Value::from("close short");
        } else if side.clone() == Value::from(3) {
            side = Value::from("open short");
        } else if side.clone() == Value::from(4) {
            side = Value::from("close long");
        };
        status = Mexc::parse_order_status(self, state.clone(), market.clone());
        let mut client_order_id: Value = self.safe_string_2(order.clone(), Value::from("client_order_id"), Value::from("orderId"), Value::Undefined);
        let mut raw_order_type: Value = self.safe_string_2(order.clone(), Value::from("orderType"), Value::from("order_type"), Value::Undefined);
        let mut order_type: Value = Value::Undefined;
        // swap: 1:price limited order, 2:Post Only Maker, 3:transact or cancel instantly, 4:transact completely or cancel completely，5:market orders, 6:convert market price to current price
        // spot: LIMIT_ORDER, POST_ONLY, IMMEDIATE_OR_CANCEL
        let mut time_in_force: Value = Value::Undefined;
        let mut post_only: Value = Value::Undefined;
        if raw_order_type.clone().is_nonnullish() {
            post_only = false.into();
            if raw_order_type.clone() == Value::from("1") {
                order_type = Value::from("limit");
                time_in_force = Value::from("GTC");
            } else if raw_order_type.clone() == Value::from("2") {
                order_type = Value::from("limit");
                time_in_force = Value::from("PO");
                post_only = true.into();
            } else if raw_order_type.clone() == Value::from("3") {
                order_type = Value::from("limit");
                time_in_force = Value::from("IOC");
            } else if raw_order_type.clone() == Value::from("4") {
                order_type = Value::from("limit");
                time_in_force = Value::from("FOK");
            } else if raw_order_type.clone() == Value::from("5") || raw_order_type.clone() == Value::from("6") {
                order_type = Value::from("market");
                time_in_force = Value::from("GTC");
            } else if raw_order_type.clone() == Value::from("LIMIT_ORDER") {
                order_type = Value::from("limit");
                time_in_force = Value::from("GTC");
            } else if raw_order_type.clone() == Value::from("POST_ONLY") {
                order_type = Value::from("limit");
                time_in_force = Value::from("PO");
                post_only = true.into();
            } else if raw_order_type.clone() == Value::from("IMMEDIATE_OR_CANCEL") {
                order_type = Value::from("limit");
                time_in_force = Value::from("IOC");
            };
        };
        return Mexc::safe_order(self, Value::Json(normalize(&Value::Json(json!({
            "id": id,
            "clientOrderId": client_order_id,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "lastTradeTimestamp": self.safe_integer(order.clone(), Value::from("updateTime"), Value::Undefined),
            "status": status,
            "symbol": symbol,
            "type": order_type,
            "timeInForce": time_in_force,
            "postOnly": post_only,
            "side": side,
            "price": price,
            "stopPrice": self.safe_string(order.clone(), Value::from("triggerPrice"), Value::Undefined),
            "average": self.safe_string(order.clone(), Value::from("dealAvgPrice"), Value::Undefined),
            "amount": amount,
            "cost": cost,
            "filled": filled,
            "remaining": remaining,
            "fee": Value::Undefined,
            "trades": Value::Undefined,
            "info": order
        }))).unwrap()), market.clone());
