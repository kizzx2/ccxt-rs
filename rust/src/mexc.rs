        Mexc::load_markets(self, Value::Undefined, Value::Undefined).await;
        let mut market: Value = Mexc::market(self, symbol.clone());
        let mut request: Value = Value::Json(normalize(&Value::Json(json!({
            "symbol": market.get(Value::from("id")),
            "states": state
        }))).unwrap());
        // 'start_time': since, // default 7 days, max 30 days
        // 'limit': limit, // default 50, max 1000
        // 'trade_type': 'BID', // BID / ASK
        // NEW, FILLED, PARTIALLY_FILLED, CANCELED, PARTIALLY_CANCELED, trigger orders: 1 untriggered, 2 cancelled, 3 executed, 4 invalid, 5 execution failed
        // 'end_time': 1633988662000, // trigger orders
        // 'page_num': 1, // trigger orders default is 1
        // 'page_size': limit, // trigger orders default 20 max 100
        let mut stop: Value = self.safe_value(params.clone(), Value::from("stop"), Value::Undefined);
        let mut limit_request: Value = if stop.is_truthy() { Value::from("page_size") } else { Value::from("limit") };
