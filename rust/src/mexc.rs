    fn parse_trade(&mut self, mut trade: Value, mut market: Value) -> Value {
        //
        // public fetchTrades
        //
        //     spot
        //
        //     {
        //         "trade_time":1633381766725,
        //         "trade_price":"0.068981",
        //         "trade_quantity":"0.005",
        //         "trade_type":"BID"
        //     }
        //
        //     swap / contract
        //
        //     {
        //         "p":3598.85,
        //         "v":52,
        //         "T":1, // 1 buy, 2 sell
        //         "O":2, // 1 opens a position, 2 does not open a position
        //         "M":2, // self-trading, 1 yes, 2 no
        //         "t":1634169038038
        //     }
        //
        // private fetchMyTrades, fetchOrderTrades
        //
        //     {
        //         "id":"b160b8f072d9403e96289139d5544809",
        //         "symbol":"USDC_USDT",
        //         "quantity":"150",
        //         "price":"0.9997",
        //         "amount":"149.955",
        //         "fee":"0.29991",
        //         "trade_type":"ASK",
        //         "order_id":"d798765285374222990bbd14decb86cd",
        //         "is_taker":true,
        //         "fee_currency":"USDT",
        //         "create_time":1633984904000
        //     }
        //
        let mut timestamp: Value = self.safe_integer_2(trade.clone(), Value::from("create_time"), Value::from("trade_time"), Value::Undefined);
        timestamp = self.safe_integer(trade.clone(), Value::from("t"), timestamp.clone());
        let mut market_id: Value = self.safe_string(trade.clone(), Value::from("symbol"), Value::Undefined);
        market = Mexc::safe_market(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut symbol: Value = market.get(Value::from("symbol"));
        let mut price_string: Value = self.safe_string_2(trade.clone(), Value::from("price"), Value::from("trade_price"), Value::Undefined);
        price_string = self.safe_string(trade.clone(), Value::from("p"), price_string.clone());
        let mut amount_string: Value = self.safe_string_2(trade.clone(), Value::from("quantity"), Value::from("trade_quantity"), Value::Undefined);
        amount_string = self.safe_string(trade.clone(), Value::from("v"), amount_string.clone());
        let mut cost_string: Value = self.safe_string(trade.clone(), Value::from("amount"), Value::Undefined);
        let mut side: Value = self.safe_string_2(trade.clone(), Value::from("trade_type"), Value::from("T"), Value::Undefined);
        if side.clone() == Value::from("BID") || side.clone() == Value::from("1") {
            side = Value::from("buy");
        } else if side.clone() == Value::from("ASK") || side.clone() == Value::from("2") {
            side = Value::from("sell");
        };
        let mut id: Value = self.safe_string_2(trade.clone(), Value::from("id"), Value::from("trade_time"), Value::Undefined);
        if id.clone().is_nullish() {
            id = self.safe_string(trade.clone(), Value::from("t"), id.clone());
            if id.clone().is_nonnullish() {
                id = id +  Value::from("-") + market.get(Value::from("id")) + Value::from("-") + amount_string.clone();
            };
        };
        let mut fee_cost_string: Value = self.safe_string(trade.clone(), Value::from("fee"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        if fee_cost_string.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string(trade.clone(), Value::from("fee_currency"), Value::Undefined);
            let mut fee_currency_code: Value = Mexc::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost_string,
                "currency": fee_currency_code
            }))).unwrap());
        };
        let mut order_id: Value = self.safe_string(trade.clone(), Value::from("order_id"), Value::Undefined);
        let mut is_taker: Value = self.safe_value(trade.clone(), Value::from("is_taker"), true.into());
        let mut taker_or_maker: Value = if is_taker.is_truthy() { Value::from("taker") } else { Value::from("maker") };
        return Mexc::safe_trade(self, Value::Json(normalize(&Value::Json(json!({
            "info": trade,
            "id": id,
            "order": order_id,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "symbol": symbol,
            "type": Value::Undefined,
            "side": side,
            "takerOrMaker": taker_or_maker,
            "price": price_string,
            "amount": amount_string,
            "cost": cost_string,
            "fee": fee
        }))).unwrap()), market.clone());
    }

